    private void inicializarComponentes() {
       this.setTitle("Ver Mensaje");
       inhabilitarComponentes();
       try{
           tfArea.setText(mensaje.getAreaSecyt().toString());
       }catch(Exception e){
           
       }
       try{
           tfinvestigador.setText(mensaje.getInvestigador().toString());
       }catch(Exception e){
           
       }
       try{
           dpFecha.setDate(mensaje.getFechaEnvio());
       }catch(Exception e){
           
       }
       try{
           taMensaje.setText(mensaje.getDescripcion());
       }catch(Exception e){
           
       }
    }

    private void cerraDialog() {
       this.dispose();
    }

    private void inhabilitarComponentes() {
       tfArea.setEditable(false);
       tfinvestigador.setEditable(false);
       dpFecha.setEditable(false);
       taMensaje.setEditable(false);
    }

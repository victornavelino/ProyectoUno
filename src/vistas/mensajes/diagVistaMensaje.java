    private void inicializarComponentes() {
        addKeyListener(new java.awt.event.KeyAdapter() {
            public void keyPressed(java.awt.event.KeyEvent evt) {
                evento(evt);
            }

            private void evento(KeyEvent evt) {
                if (evt.getKeyCode() == KeyEvent.VK_F5) {
                    JOptionPane.showMessageDialog(null, "presionaste: " + evt.getKeyCode());

                }
            }
        });
        if(verMensajeEnviado){
           bloquearComponentes();
           cargarDatos();
        }else{
           cargarDatos(); 
        }
        
        


    }

    private void cerrar() {
        this.dispose();
    }

    private void responderMensaje() {
        diagEmail diagEmail = new diagEmail(null, true, mensaje);
        diagEmail.setLocation(Comunes.centrarDialog(diagEmail));
        diagEmail.setVisible(true);
    }

    private void mover() {
        if (cboAreas.getSelectedIndex() > 0) {
            try {
                AreaSecyt areaSecyt = (AreaSecyt) cboAreas.getSelectedItem();
                mensaje.setAreaSecyt(areaSecyt);
                Mensaje mensaje_copia = new Mensaje();
                mensaje_copia = mensaje;
                mensaje_copia.setLeido(false);
                MensajeFacade.getInstance().modificar(mensaje);
                MensajeFacade.getInstance().alta(mensaje_copia);
                JOptionPane.showMessageDialog(rootPane, "Se envío el mensaje a " + areaSecyt);
                this.dispose();

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, "Error moviendo mensaje");
            }
        }
    }

    private void cargarDatos() {
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        Comunes.autoAjusteTextArea(taMensaje);
        tfEmail.setEditable(false);
        taMensaje.setEditable(false);
        try {
            lbAreaSecyt.setText(mensaje.getAreaSecyt().toString());
        } catch (Exception ex) {
        }
        try {
            lbInvestigador.setText(mensaje.getInvestigador().toString());
        } catch (Exception ex) {
        }
        try {
            tfEmail.setText(String.valueOf(mensaje.getInvestigador().getPersona().getCorreosElectronicos().get(0)));
        } catch (Exception ex) {
        }
        try {
            lbFecha.setText(formato.format(mensaje.getFechaEnvio()));
        } catch (Exception ex) {
        }
        try {
            taMensaje.setText(mensaje.getDescripcion());
        } catch (Exception ex) {
        }
        try {
            Comunes.cargarJComboConBlanco(cboAreas, AreaSecytFacade.getInstance().listar());
        } catch (Exception ex) {
            Logger.getLogger(diagMensajes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void bloquearComponentes() {
        btnResponderMensaje.setVisible(false);
        jLabel5.setVisible(false);
        cboAreas.setVisible(false);
        jButton1.setVisible(false);
    }

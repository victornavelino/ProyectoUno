    private void buscarInvestigador(String text) {
        if("".equals(text)){
            Comunes.cargarJList(jList1, investigadoresCat1y2);
             lblSuperior.setText("Investigadores con categorÃ­a 1 o 2");
        }
        else{
        List<Investigador> investigadoresEncontrados = InvestigadorFacade.getInstance().buscarDniApellido(text);
        Comunes.cargarJList(jList1, investigadoresEncontrados);
        lblSuperior.setText("Investigadores encontrados");
        }
       }

    private void escogerInvestigador() {
        if (jList1.getSelectedIndex() != -1) {
            investigador = (Investigador) jList1.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un investigador de la lista",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    public Investigador getInvestigador() {
        return this.investigador;
    }

    private void cargarInvestigadoresCat1y2() {
        List<Investigador> investigadores = InvestigadorFacade.getInstance().getTodosInvestigador();
        for (Investigador investigadorcat : investigadores) {

            List<Categorizacion> categorias = investigadorcat.getCategorizaciones();
            for (Categorizacion categorizacion : categorias) {
                CategoriaAsignada categoriaAsignada = categorizacion.getCategoriaAsignada();

                if (categoriaAsignada != null) {
                    if (categoriaAsignada.getValorCategoria().getDescripcion().equals("1")
                            || categoriaAsignada.getValorCategoria().getDescripcion().equals("2")) {
                        investigadoresCat1y2.add(investigadorcat);
                        break;

                    }
                }
            }

        }
        Comunes.cargarJList(jList1, investigadoresCat1y2);

    }

    private void cambiarEstado() {
        if (chkTodosInv.isSelected()) {
            cargarInvestigadoresTodos();
            lblSuperior.setText("Todos los investigadores");
        } else {
            cargarInvestigadoresCat1y2();
            lblSuperior.setText("Investigadores con categorÃ­a 1 o 2");
        }

    }

    private void cargarInvestigadoresTodos() {
        List<Investigador> investigadoresTodos = InvestigadorFacade.getInstance().getTodosInvestigador();
        Comunes.cargarJList(jList1, investigadoresTodos);
    }

    private void nuevoInvestigador() {
        diagInvestigador investigadorAlta = new diagInvestigador(null, true, "Alta", usuario);
        investigadorAlta.setLocation(Comunes.centrarDialog(investigadorAlta));
        investigadorAlta.setVisible(true);
        cargarInvestigadoresTodos();
        lblSuperior.setText("Todos los investigadores");
        
    }

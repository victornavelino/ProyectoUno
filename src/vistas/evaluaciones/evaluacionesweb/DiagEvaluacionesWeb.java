    private void agregar() {
        DiagEvaluacionWeb evaluadorEleccion = new DiagEvaluacionWeb(null, true, usuario);
        evaluadorEleccion.setLocation(Comunes.centrarDialog(evaluadorEleccion));
        evaluadorEleccion.setVisible(true);
        
        if (evaluadorEleccion.getEvaluacionesWebs() != null) {
            evaluacionesWeb.addAll(evaluadorEleccion.getEvaluacionesWebs());
            
        }
    }

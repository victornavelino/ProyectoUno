    private void inicializarComponentes() {
        Comunes.cargarJComboConBlanco(cboTipoEvaluacion, TipoEvaluacionFacade.getInstance().listarTodosTipoEvaluacionOrdenados());
    }

    private void buscarProyecto() {
        diagProyectoBusquedaSimple selecproyecto = new diagProyectoBusquedaSimple(null, true);
        selecproyecto.setLocation(Comunes.centrarDialog(selecproyecto));
        selecproyecto.setVisible(true);
        if (selecproyecto.getProyecto() != null) {
            proyecto = selecproyecto.getProyecto();
            taProyecto.setText(proyecto.toString());

        }

    }

    private void agregarEvaluador() {
        diagEvaluadorBusqueda evaluadorEleccion = new diagEvaluadorBusqueda(null, true, usuario);
        evaluadorEleccion.setLocation(Comunes.centrarDialog(evaluadorEleccion));
        evaluadorEleccion.setVisible(true);

        if (evaluadorEleccion.getEvaluador() != null) {
            evaluadores.add(evaluadorEleccion.getEvaluador());
            
        }
    }

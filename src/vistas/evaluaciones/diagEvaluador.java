    private void cargarInvestigador() {
        if (evaluador != null) {
            try {
                tfApellidos.setText(evaluador.getInvestigador().getPersona().getApellido());
            } catch (java.lang.NullPointerException ex) {
            }
            try {

                tfBarrio.setText(evaluador.getInvestigador().getPersona().getDomicilio().getBarrio());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfCalle.setText(evaluador.getInvestigador().getPersona().getDomicilio().getCalle());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfCodigoPostal.setText(evaluador.getInvestigador().getPersona().getDomicilio().getCodigoPostal());
            } catch (java.lang.NullPointerException ex) {
            }
             try {
                jXDateFechaNacimiento.setDate(evaluador.getInvestigador().getPersona().getFechaNacimiento());
            } catch (java.lang.NullPointerException ex) {
                jXDateFechaNacimiento.setDate(null);
            }
            try {
                tfCuil.setText(evaluador.getInvestigador().getPersona().getCuil());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfDpto.setText(evaluador.getInvestigador().getPersona().getDomicilio().getDpto());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfEntreCalles.setText(evaluador.getInvestigador().getPersona().getDomicilio().getEntreCalles());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfNombres.setText(evaluador.getInvestigador().getPersona().getNombre());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfNumero.setText(evaluador.getInvestigador().getPersona().getDomicilio().getNumero());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                cboTipoDocumento.setSelectedItem(evaluador.getInvestigador().getPersona().getDocumentoIdentidad().getTipoDocumento());
            } catch (java.lang.NullPointerException ex) {
                cboTipoDocumento.addItem("--Seleccione--");
                cboTipoDocumento.setSelectedItem("--Seleccione--");
            }
            try {
                tfNumeroDocumento.setText(((Long) evaluador.getInvestigador().getPersona().getDocumentoIdentidad().getNumero()).toString());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfPiso.setText(evaluador.getInvestigador().getPersona().getDomicilio().getPiso());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                tfReferencia.setText(evaluador.getInvestigador().getPersona().getDomicilio().getReferencia());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                Comunes.cargarJList(jListTelefonos, evaluador.getInvestigador().getPersona().getTelefonos());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                Comunes.cargarJList(jListCorreosElectronicos, evaluador.getInvestigador().getPersona().getCorreosElectronicos());
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                cboProvincias.setSelectedItem(evaluador.getInvestigador().getPersona().getDomicilio().getLocalidad().getDepartamento().getProvincia());
                cargarDepartamentos();
            } catch (java.lang.NullPointerException ex) {
                cboProvincias.addItem("--Seleccione--");
                cboProvincias.setSelectedItem("--Seleccione--");
                cboDepartamentos.addItem("--Seleccione--");
                cboDepartamentos.setSelectedItem("--Seleccione--");
                cboLocalidades.addItem("--Seleccione--");
                cboLocalidades.setSelectedItem("--Seleccione--");
            }
            try {
                cboDepartamentos.setSelectedItem(evaluador.getInvestigador().getPersona().getDomicilio().getLocalidad().getDepartamento());
                cargarLocalidades();
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                if (evaluador.getInvestigador().getPersona().getDomicilio().getLocalidad() != null) {
                    cboLocalidades.setSelectedItem(evaluador.getInvestigador().getPersona().getDomicilio().getLocalidad());
                }
            } catch (java.lang.NullPointerException ex) {
            }
            try {
                Comunes.cargarJList(jListCorreosElectronicos, evaluador.getInvestigador().getPersona().getCorreosElectronicos());
            } catch (java.lang.NullPointerException ex) {
            }

        }
    }

    private void agregarTelefono() {
        Telefono telefono = new Telefono();
        telefono.setNumero(tfNumeroTelefono.getText());
        telefono.setTipoTelefono((TipoTelefono) cboTiposTelefonos.getSelectedItem());
        telefonos.add(telefono);
        Comunes.cargarJList(jListTelefonos, telefonos);


    }

    private void agregarCorreoElectronico() {
        CorreoElectronico correoElectronico = new CorreoElectronico();
        correoElectronico.setDireccion(tfCorreoElectronico.getText());
        correosElectronicos.add(correoElectronico);
        Comunes.cargarJList(jListCorreosElectronicos, correosElectronicos);


    }

    private void eliminarTelefono() {
        if (jListTelefonos.getSelectedIndex() != -1) {
            telefonos.remove((Telefono) jListTelefonos.getSelectedValue());
            Comunes.cargarJList(jListTelefonos, telefonos);


        }
    }

    private void eliminarCorreoElectronico() {
        if (jListCorreosElectronicos.getSelectedIndex() != -1) {
            correosElectronicos.remove((CorreoElectronico) jListCorreosElectronicos.getSelectedValue());
            Comunes.cargarJList(jListCorreosElectronicos, correosElectronicos);

        }
    }


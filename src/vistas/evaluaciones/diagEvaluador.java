    private void aceptar() {
        if (tipoOperacion.equals("Modificación")) {
            if (evaluador != null) {
                evaluador.getInvestigador().getPersona().setApellido(tfApellidos.getText());
                evaluador.getInvestigador().getPersona().setNombre(tfNombres.getText());
                if (!tfCuil.getText().equals("")) {
                    evaluador.getInvestigador().getPersona().setCuil(tfCuil.getText());
                }
                if (jXDateFechaNacimiento.getDate() != null) {
                    evaluador.getInvestigador().getPersona().setFechaNacimiento(jXDateFechaNacimiento.getDate());
                }
                Domicilio domicilio = evaluador.getInvestigador().getPersona().getDomicilio();
                domicilio.setCalle(tfCalle.getText());
                domicilio.setNumero(tfNumero.getText());
                domicilio.setPiso(tfPiso.getText());
                domicilio.setDpto(tfDpto.getText());
                domicilio.setLocalidad((Localidad) cboLocalidades.getSelectedItem());
                domicilio.setCodigoPostal(tfCodigoPostal.getText());
                domicilio.setEntreCalles(tfEntreCalles.getText());
                domicilio.setReferencia(tfReferencia.getText());
                domicilio.setBarrio(tfBarrio.getText());
                evaluador.getInvestigador().getPersona().setDomicilio(domicilio);
                evaluador.getInvestigador().getPersona().setTelefonos(telefonos);
                evaluador.getInvestigador().getPersona().setCorreosElectronicos(correosElectronicos);
                DocumentoIdentidad documentoIdentidad = evaluador.getInvestigador().getPersona().getDocumentoIdentidad();
                documentoIdentidad.setNumero(Long.parseLong(tfNumeroDocumento.getText()));
                documentoIdentidad.setTipoDocumento((TipoDocumento) cboTipoDocumento.getSelectedItem());
                evaluador.getInvestigador().getPersona().setDocumentoIdentidad(documentoIdentidad);

                JOptionPane.showMessageDialog(null, "Evaluador modificada con éxito");
                this.dispose();
            }
        } else if (tipoOperacion.equals("Alta")) {
            evaluador = new Evaluador();
            evaluador.getInvestigador().getPersona().setApellido(tfApellidos.getText());
            evaluador.getInvestigador().getPersona().setNombre(tfNombres.getText());
            if (!tfCuil.getText().equals("")) {
                evaluador.getInvestigador().getPersona().setCuil(tfCuil.getText());
            }
            if (jXDateFechaNacimiento.getDate() != null) {
                evaluador.getInvestigador().getPersona().setFechaNacimiento(jXDateFechaNacimiento.getDate());
            }
            Domicilio domicilio = new Domicilio();
            domicilio.setCalle(tfCalle.getText());
            domicilio.setNumero(tfNumero.getText());
            domicilio.setPiso(tfPiso.getText());
            domicilio.setDpto(tfDpto.getText());
            domicilio.setLocalidad((Localidad) cboLocalidades.getSelectedItem());
            domicilio.setCodigoPostal(tfCodigoPostal.getText());
            domicilio.setEntreCalles(tfEntreCalles.getText());
            domicilio.setReferencia(tfReferencia.getText());
            domicilio.setBarrio(tfBarrio.getText());
            evaluador.getInvestigador().getPersona().setDomicilio(domicilio);
            evaluador.getInvestigador().getPersona().setTelefonos(telefonos);
            evaluador.getInvestigador().getPersona().setCorreosElectronicos(correosElectronicos);
            DocumentoIdentidad documentoIdentidad = new DocumentoIdentidad();
            documentoIdentidad.setNumero(Long.parseLong(tfNumeroDocumento.getText()));
            documentoIdentidad.setTipoDocumento((TipoDocumento) cboTipoDocumento.getSelectedItem());
            evaluador.getInvestigador().getPersona().setDocumentoIdentidad(documentoIdentidad);
            EvaluadorFacade.getInstance().alta(evaluador);
            JOptionPane.showMessageDialog(null, "Evaluador creada con éxito");
            this.dispose();
        } else if (tipoOperacion.equals("Consulta")) {
            this.dispose();
        }

    }


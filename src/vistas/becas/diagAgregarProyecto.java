    private void inicializarcomponentes() {
        this.proyecto=null;
        List<Proyecto> proyectopostulante=new ArrayList<Proyecto>();
        List<Participacion> proyetos=postulante.getParticipacionesProyecto();
        for(Participacion participacion:proyetos){
            proyectopostulante.add(participacion.getProyecto());
}
        Comunes.cargarJList(jListProyectos, proyectopostulante);
    }

    private void buscarproyecto() {
         if (!tfBuscarProyecto.getText().equals("")) {
            Comunes.cargarJList(jListProyectos, proyectoFacade.buscar(tfBuscarProyecto.getText()));
        } else {
            Comunes.cargarJList(jListProyectos, proyectoFacade.getTodos());
        } 
    }

    private void seleccionarproyecto() {
        if(jListProyectos.getSelectedValue()!=null){
        
        proyecto=new Proyecto();
        this.proyecto=((Proyecto)jListProyectos.getSelectedValue());
        this.dispose();
        
        }
        else JOptionPane.showMessageDialog(null, "Debe escoger un Proyecto", "Error", JOptionPane.ERROR_MESSAGE);
        
        }

    public Proyecto getProyecto() {
        return proyecto;
    }



    

   



  

  

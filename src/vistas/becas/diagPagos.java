    private void inicializarComponentes() {
        Comunes.formatearJXdatePicker(jxdpFecha);
        Comunes.autoAjusteTextArea(taResolucion);
        Comunes.autoAjusteTextArea(taObservaciones);
        if (!NumPagoFacade.getInstance().getTodas().isEmpty()) {
            Comunes.cargarJComboConBlanco(cboNroPago, NumPagoFacade.getInstance().getTodas());
        }


        if (tipoOperacion.equals("Modificación")) {
            try {
                if (pago.getExpediente() != null) {
                    tfexpediente.setText(pago.getExpediente());
                }
            } catch (Exception ex) {
            }
            try {
                if (pago.getResolucion() != null) {
                    taResolucion.setText(pago.getResolucion().toString());
                }
            } catch (Exception ex) {
            }
            try {
                if (pago.getMonto() != -1) {
                    tfMonto.setText(String.valueOf(pago.getMonto()));
                }
            } catch (Exception ex) {
            }
            try {
                if (!pago.getNroCheque().isEmpty()) {
                    tfNroCheque.setText(pago.getNroCheque());
                }
            } catch (Exception ex) {
            }
            try {
                if (pago.getNumPago() != null) {
                    cboNroPago.setSelectedItem(pago.getNumPago());
                }
            } catch (Exception ex) {
            }
            try {
                if (!pago.getObservaciones().isEmpty()) {
                    taObservaciones.setText(pago.getObservaciones());
                }
            } catch (Exception ex) {
            }
            try {
                
                    jxdpFecha.setDate(pago.getFecha());
                
            } catch (Exception ex) {
                jxdpFecha.setDate(null);
            }
        }

    }

    private void agregarResolucion() {
        diagResolucionEleccion resolucionEleccion = new diagResolucionEleccion(null, true);
        resolucionEleccion.setLocation(Comunes.centrarDialog(resolucionEleccion));
        resolucionEleccion.setVisible(true);
        if (resolucionEleccion.getResolucion() != null) {
            resolucion = new Resolucion();
            resolucion = resolucionEleccion.getResolucion();
            taResolucion.setText(resolucion.toString());

        }

    }

    private void aceptar() {
        if (validar()) {
            if (tipoOperacion.equals("Alta")) {
                pago = new Pago();

                if (!tfexpediente.getText().isEmpty()) {
                    pago.setExpediente(tfexpediente.getText());
                } else {
                    pago.setExpediente(null);
                }
                if (!tfMonto.getText().isEmpty()) {
                    pago.setMonto(Float.parseFloat(tfMonto.getText()));
                } else {
                    pago.setMonto(0);
                }
                if (!tfNroCheque.getText().isEmpty()) {
                    pago.setNroCheque(tfNroCheque.getText());
                } else {
                    pago.setNroCheque(null);
                }
                pago.setNumPago((NumPago) cboNroPago.getSelectedItem());
                pago.setFecha(jxdpFecha.getDate());

                if (!taObservaciones.getText().isEmpty()) {
                    pago.setObservaciones(taObservaciones.getText());
                } else {
                    pago.setObservaciones(null);
                }
                if (!taResolucion.getText().isEmpty()) {
                    pago.setResolucion(resolucion);
                } else {
                    pago.setResolucion(null);
                }
                pago.setPostulacionbeca(postulacion);


                PagoFacade.getInstance().alta(pago);
                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Alta de Pago de Beca");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);
                JOptionPane.showMessageDialog(null, "Pago Registrado");
                this.dispose();

            }
            if (tipoOperacion.equals("Modificación")) {


                if (!tfexpediente.getText().isEmpty()) {
                    pago.setExpediente(tfexpediente.getText());
                } else {
                    pago.setExpediente(null);
                }
                if (!tfMonto.getText().isEmpty()) {
                    pago.setMonto(Float.parseFloat(tfMonto.getText()));
                } else {
                    pago.setMonto(0);
                }
                if (!tfNroCheque.getText().isEmpty()) {
                    pago.setNroCheque(tfNroCheque.getText());
                } else {
                    pago.setNroCheque(null);
                }
                pago.setNumPago((NumPago) cboNroPago.getSelectedItem());
                pago.setFecha(jxdpFecha.getDate());

                if (!taObservaciones.getText().isEmpty()) {
                    pago.setObservaciones(taObservaciones.getText());
                } else {
                    pago.setObservaciones(null);
                }
                if (!taResolucion.getText().isEmpty()) {
                    pago.setResolucion(resolucion);
                } else {
                    pago.setResolucion(null);
                }
                pago.setPostulacionbeca(postulacion);

                PagoFacade.getInstance().modificar(pago);
                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Modificación de Pago de Beca");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);
                JOptionPane.showMessageDialog(null, "Modificacion Registrada");
                this.dispose();
            }
        }

    }

    private boolean validar() {
        boolean flag = false;

        if (cboNroPago.getSelectedIndex() > 0) {
            if (tfMonto.getText().isEmpty() || Comunes.validarBigDecimal(tfMonto.getText())) {
                flag = true;
            } else {
                JOptionPane.showMessageDialog(null, "Formato numerico invalido");
                tfMonto.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar nro de pago");
            cboNroPago.requestFocus();
        }


        return flag;
    }


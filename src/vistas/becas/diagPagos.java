    private void agregarNumPago() {
        diagNumPago nuevoNumPago = new diagNumPago(null, true);
        nuevoNumPago.setLocation(Comunes.centrarDialog(nuevoNumPago));
        nuevoNumPago.setVisible(true);
        if (nuevoNumPago.getNumPago() != null) {
            Comunes.cargarJCombo(cboNroPago, NumPagoFacade.getInstance().getTodas());
        }
    }

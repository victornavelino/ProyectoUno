    private void aceptar() {
        if (!tfNumPago.getText().isEmpty()) {
            numPago=new NumPago();
            numPago.setDescripcion(tfNumPago.getText());
            NumPagoFacade.getInstance().alta(numPago);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar descripcion!!");
        }

    }

    private void guardardatos() {
        if (validardatos()) {
            if (tipoOperacion.equals("Alta")) {
                beca = new Becas();
                if (!tfCodigoBeca.getText().equals("")) {
                    beca.setCodigobeca(tfCodigoBeca.getText());
                } else {
                    beca.setCodigobeca(null);
                }
                if (!tfDescripcion.getText().equals("")) {
                    beca.setDescripcion(tfDescripcion.getText());
                } else {
                    beca.setDescripcion(null);
                }
                if (jXDateFechainicio.getDate() != null) {
                    beca.setFechainicio(jXDateFechainicio.getDate());
                } else {
                    beca.setFechainicio(null);
                }
                if (jXDateFechaFinal.getDate() != null) {
                    beca.setFechafinalizacion(jXDateFechaFinal.getDate());
                } else {
                    beca.setFechafinalizacion(null);
                }
                if (!tfDuracion.getText().equals("")) {
                    beca.setDuracion(Integer.parseInt(tfDuracion.getText()));
                } else {
                    beca.setDuracion(null);
                }
                if (!tfCantpagos.getText().equals("")) {
                    beca.setCantidadpagos(Integer.parseInt(tfCantpagos.getText()));
                } else {
                    beca.setCantidadpagos(null);
                }
                if (!tfCantbeneficiarios.getText().equals("")) {
                    beca.setCantidadbeneficiarios(Integer.parseInt(tfCantbeneficiarios.getText()));
                } else {
                    beca.setCantidadbeneficiarios(null);
                }
                if (!tfMonto.getText().equals("")) {
                    beca.setMontoporpago(Integer.parseInt(tfMonto.getText()));
                } else {
                    beca.setMontoporpago(null);
                }
                if(!cboTipoBeca.getSelectedItem().equals("")){
                    beca.setTipoBeca(cboTipoBeca.getSelectedItem().toString());
                }else{
                    beca.setTipoBeca(null);
                }
                beca.setParaalumnos(ckAlumnos.isSelected());
                beca.setParadocentes(ckDocentes.isSelected());
                beca.setTieneproyecto(ckProyectos.isSelected());
                beca.setTienedirector(ckDirector.isSelected());
                beca.setTieneposgrado(ckPosgrado.isSelected());
                beca.setTienecarrera(ckCarrera.isSelected());
                if (cbVigente.getSelectedItem().toString().equals("SI")) {
                    beca.setVigente(true);
                } else {
                    beca.setVigente(false);
                }



                BecasFacade.getInstance().alta(beca);
                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Agregacion de Beca");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);
                this.dispose();
            } else if (tipoOperacion.equals("Modificación")) {

                if (!tfCodigoBeca.getText().equals("")) {
                    beca.setCodigobeca(tfCodigoBeca.getText());
                } else {
                    beca.setCodigobeca(null);
                }
                if (!tfDescripcion.getText().equals("")) {
                    beca.setDescripcion(tfDescripcion.getText());
                } else {
                    beca.setDescripcion(null);
                }
                if (jXDateFechainicio.getDate() != null) {
                    beca.setFechainicio(jXDateFechainicio.getDate());
                } else {
                    beca.setFechainicio(null);
                }
                if (jXDateFechaFinal.getDate() != null) {
                    beca.setFechafinalizacion(jXDateFechaFinal.getDate());
                } else {
                    beca.setFechafinalizacion(null);
                }
                if (!tfDuracion.getText().equals("")) {
                    beca.setDuracion(Integer.parseInt(tfDuracion.getText()));
                } else {
                    beca.setDuracion(null);
                }
                if (!tfCantpagos.getText().equals("")) {
                    beca.setCantidadpagos(Integer.parseInt(tfCantpagos.getText()));
                } else {
                    beca.setCantidadpagos(null);
                }
                if (!tfCantbeneficiarios.getText().equals("")) {
                    beca.setCantidadbeneficiarios(Integer.parseInt(tfCantbeneficiarios.getText()));
                } else {
                    beca.setCantidadbeneficiarios(null);
                }
                if (!tfMonto.getText().equals("")) {
                    beca.setMontoporpago(Integer.parseInt(tfMonto.getText()));
                } else {
                    beca.setMontoporpago(null);
                }
                beca.setParaalumnos(ckAlumnos.isSelected());
                beca.setParadocentes(ckDocentes.isSelected());
                beca.setTieneproyecto(ckProyectos.isSelected());
                beca.setTienedirector(ckDirector.isSelected());
                beca.setTieneposgrado(ckPosgrado.isSelected());
                beca.setTienecarrera(ckCarrera.isSelected());
                if(!cboTipoBeca.getSelectedItem().equals("")){
                    beca.setTipoBeca(cboTipoBeca.getSelectedItem().toString());
                }else{
                    beca.setTipoBeca(null);
                }
                
                if (cbVigente.getSelectedItem().toString().equals("SI")) {
                    beca.setVigente(true);
                } else {
                    beca.setVigente(false);
                }
                BecasFacade.getInstance().modificar(beca);
                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Modificación de Beca");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);
                JOptionPane.showMessageDialog(null, "Modificado con éxito");
                this.dispose();
            }






        }
    }

    public boolean validarFecha(String fecha) {


        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

        if (fecha.trim().length() != dateFormat.toPattern().length()) {
            return false;
        }

        dateFormat.setLenient(false);

        try {
            dateFormat.parse(fecha.trim());
        } catch (ParseException pe) {
            return false;
        }
        return true;
    }


    private void inicializarComponentes() {
        cargarCombox();
        Comunes.cargarJComboConBlanco(cboEvaluadores, InvestigadorFacade.getInstance().getTodosInvestigador());
        Comunes.formatearJXdatePicker(jxdpFecha);

        if (tipoOperacion.equals("Modificación")) {
            try {
                if (evaluacion.getEvaluador() != null) {
                    cboEvaluadores.setSelectedItem(evaluacion.getEvaluador());
                }
            } catch (Exception ex) {
            }
            try {
                if (evaluacion.getInstitucion() != null) {
                    cboUniversidad.setSelectedItem(evaluacion.getInstitucion());
                }
            } catch (Exception ex) {
            }
            try {
                if (evaluacion.getEvaluacionEstado() != null) {
                    cboEstadoBeca.setSelectedItem(evaluacion.getEvaluacionEstado());
                }
            } catch (Exception ex) {
            }
            try {
                if (!evaluacion.getObservaciones().isEmpty()) {
                    taObservaciones.setText(evaluacion.getObservaciones());
                }
            } catch (Exception ex) {
            }
            try {
                
                    jxdpFecha.setDate(evaluacion.getFecha());
                
            } catch (Exception ex) {
                jxdpFecha.setDate(null);
            }
            try {
                if (evaluacion.getCalificacionEnLetras() != null) {
                    cboCalificacion.setSelectedItem(evaluacion.getCalificacionEnLetras());
                }
            } catch (Exception ex) {
            }
            try {
                if (evaluacion.getCalificacionEnNumeros() > -1) {
                    tfCalificacionNumero.setText(String.valueOf(evaluacion.getCalificacionEnNumeros()));
                }
            } catch (Exception ex) {
            }
        }

    }


    private void aceptar() {
        if (validar()) {
            if (tipoOperacion.equals("Alta")) {
                evaluacion = new Evaluaciones();
                evaluacion.setPostulacionBeca(postulacion);
                evaluacion.setEvaluador((Investigador) cboEvaluadores.getSelectedItem());
                evaluacion.setEvaluacionEstado((EvaluacionEstado) cboEstadoBeca.getSelectedItem());
                if (cboUniversidad.getSelectedIndex() > 0) {
                    evaluacion.setInstitucion((Universidad) cboUniversidad.getSelectedItem());
                } else {
                    evaluacion.setInstitucion(null);
                }
                if(jxdpFecha.getDate()!=null){
                    evaluacion.setFecha(jxdpFecha.getDate());
                }else{
                    evaluacion.setFecha(null);
                }
                
                if (!taObservaciones.getText().isEmpty()) {
                    evaluacion.setObservaciones(taObservaciones.getText());
                } else {
                    evaluacion.setObservaciones(null);
                }
                if (cboCalificacion.getSelectedIndex() > 0) {
                    evaluacion.setCalificacionEnLetras((Calificacion) cboCalificacion.getSelectedItem());
                } else {
                    evaluacion.setCalificacionEnLetras(null);
                }
                if (!tfCalificacionNumero.getText().isEmpty()) {
                    evaluacion.setCalificacionEnNumeros(Float.parseFloat(tfCalificacionNumero.getText()));
                } else {
                    evaluacion.setCalificacionEnNumeros(0);
                }
                EvaluacionesFacade.getInstance().alta(evaluacion);
                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Alta de Evaluacion de Beca");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);
                JOptionPane.showMessageDialog(null, "Evaluacion Registrada");
                this.dispose();

            }
            if (tipoOperacion.equals("Modificación")) {


                evaluacion.setEvaluador((Investigador) cboEvaluadores.getSelectedItem());
                evaluacion.setEvaluacionEstado((EvaluacionEstado) cboEstadoBeca.getSelectedItem());
                if (cboUniversidad.getSelectedIndex() > -1) {
                    evaluacion.setInstitucion((Universidad) cboUniversidad.getSelectedItem());
                } else {
                    evaluacion.setInstitucion(null);
                }
                if(jxdpFecha.getDate()!=null){
                    evaluacion.setFecha(jxdpFecha.getDate());
                }else{
                    evaluacion.setFecha(null);
                }
                if (!taObservaciones.getText().isEmpty()) {
                    evaluacion.setObservaciones(taObservaciones.getText());
                } else {
                    evaluacion.setObservaciones(null);
                }
                if (cboCalificacion.getSelectedIndex() > 0) {
                    evaluacion.setCalificacionEnLetras((Calificacion) cboCalificacion.getSelectedItem());
                } else {
                    evaluacion.setCalificacionEnLetras(null);
                }
                if (!tfCalificacionNumero.getText().isEmpty()) {
                    evaluacion.setCalificacionEnNumeros(Float.parseFloat(tfCalificacionNumero.getText()));

                } else {
                    evaluacion.setCalificacionEnNumeros(0);
                }
                EvaluacionesFacade.getInstance().modificar(evaluacion);
                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Modificacion de Evaluacion de Beca");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);
                JOptionPane.showMessageDialog(null, "Evaluacion Modificada");
                this.dispose();
            }
        }

    }

    private boolean validar() {
        boolean flag = false;
        if (cboEvaluadores.getSelectedIndex() > 0) {
            if (cboEstadoBeca.getSelectedIndex() > 0) {
                if (tfCalificacionNumero.getText().isEmpty() || Comunes.validarBigDecimal(tfCalificacionNumero.getText())) {
                    flag = true;
                } else {
                    JOptionPane.showMessageDialog(null, "La evaluacion debe tener una calificacion");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe seleccionar la situacion de la Beca");
                cboEstadoBeca.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un evaluador");
            cboEvaluadores.requestFocus();
        }
        return flag;
    }


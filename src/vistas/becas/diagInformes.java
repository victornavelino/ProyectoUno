    private void exportarSegunSeleccionCbo() {
        switch (cboExportarAExcel.getSelectedIndex()) {
            case 0:
                PostulacionBecaFacade.getInstance().exportarAExcelPostulantes();
                break;
            case 1:
                EvaluacionesFacade.getInstance().exportarAExcelEvaluaciones();
                break;
            case 2:
                NotaPagoFacade.getInstance().exportarAExcelPagos();
                break;
            case 3:
                PostulacionBecaFacade.getInstance().exportarAExcelSituacionPostulantes();
                break;
            case 4:
                PostulacionBecaFacade.getInstance().exportarAExcelTodoBecas();
                break;
        }

    }

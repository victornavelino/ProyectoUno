    private void cargarTabla(String buscar) {

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Object[] fila = new Object[6];
        modelo = new DefaultTableModel();
        modelo.addColumn("Beca");
        modelo.addColumn("Proyecto");
        modelo.addColumn("Plan de Trabajo");
        modelo.addColumn("Postulante");
        modelo.addColumn("Situacion");
        modelo.addColumn("Fecha");

        for (PostulacionBeca postulaciones : PostulacionBecaFacade.getInstance().getPostulante(buscar)) {

            try {
                fila[0] = postulaciones.getBeca().getDescripcion();
            } catch (Exception ex) {
            }
            try {
                fila[1] = postulaciones.getProyecto().toString();
            } catch (Exception ex) {
            }
            try {
                fila[2] = postulaciones.getPlandetrabajo();
            } catch (Exception ex) {
            }
            try {
                fila[3] = postulaciones.getPostulante().toString();
            } catch (Exception ex) {
            }
            try {
                fila[4] = postulaciones.getPostulanteestado();
            } catch (Exception ex) {
            }
            try {
                fila[5] = sdf.format(postulaciones.getFechaalta());
            } catch (Exception ex) {
            }
            modelo.addRow(fila);

        }
        jTable1.setModel(modelo);

        //Comunes.setOcultarColumnasJTable(jTable1, oculta);
    }


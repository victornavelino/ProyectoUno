    private void aceptar() {
        if (tipoOperacionAsignatura.equals("Alta")) {
            if (validar()) {
                asignatura = new Asignatura();
                asignatura.setCarreraAsignatura((CarreraAsignatura) cboCarrera.getSelectedItem());
                asignatura.setCodigoAsignatura(tfCodigo.getText());
                asignatura.setCodigoAsignaturaNacion(tfCodigoNacion.getText());
                asignatura.setDescripcion(tfNombreAsignatura.getText());
                asignatura.setFacultadAsignatura((FacultadAsignatura) cboFacultad.getSelectedItem());
                AsignaturaFacade.getInstance().alta(asignatura);
                this.dispose();
                
            }
        }
        if (tipoOperacionAsignatura.equals("Modificación")) {

            asignatura.setCarreraAsignatura((CarreraAsignatura) cboCarrera.getSelectedItem());
            asignatura.setCodigoAsignatura(tfCodigo.getText());
            asignatura.setCodigoAsignaturaNacion(tfCodigoNacion.getText());
            asignatura.setDescripcion(tfNombreAsignatura.getText());
            asignatura.setFacultadAsignatura((FacultadAsignatura) cboFacultad.getSelectedItem());
            AsignaturaFacade.getInstance().modificar(asignatura);
            this.dispose();

        }

    }

    private void inicializarComponentes() {
        Comunes.cargarJCombo(cboCarrera, CarreraAsignaturaFacade.getInstance().listarTodosCarreraAsignaturaOrdenados());
        Comunes.cargarJCombo(cboFacultad, FacultadAsignaturaFacade.getInstance().listarTodosFacultadAsignaturaOrdenados());
        if(tipoOperacionAsignatura.equals("Alta")){
            btnEliminarAsignatura.setVisible(false);
        }
        if (tipoOperacionAsignatura.equals("Modificación")) {
            tfCodigo.setEditable(false);
            try {
                tfCodigo.setText(asignatura.getCodigoAsignatura());
            } catch (Exception ex) {
                tfCodigo.setText("");
            }
            try {
                tfCodigoNacion.setText(asignatura.getCodigoAsignaturaNacion());
            } catch (Exception ex) {
                tfCodigoNacion.setText("");
            }
            try {
                cboCarrera.setSelectedItem(asignatura.getCarreraAsignatura());
            } catch (Exception ex) {
            }
            try {
                cboFacultad.setSelectedItem(asignatura.getFacultadAsignatura());
            } catch (Exception ex) {
            }
            try {
                tfNombreAsignatura.setText(asignatura.getDescripcion());
            } catch (Exception ex) {
                tfNombreAsignatura.setText("");
            }

        }
    }

    private boolean validar() {
        boolean flag = false;
        if (!tfCodigo.getText().isEmpty()) {
            if (!AsignaturaFacade.getInstance().existeCodigo(tfCodigo.getText())) {
                if (!tfNombreAsignatura.getText().isEmpty()) {
                    flag = true;
                }else{
                JOptionPane.showMessageDialog(null, "La Asignatura debe poseer un nombre!!"); 
                   flag=false;
                }

            } else {
                JOptionPane.showMessageDialog(null, "Ya existe el Asignatura para ese codigo!!");
                flag = false;
            }
        }
        return flag;
    }

    private void cancelar() {
        this.dispose();
    }

    private void eliminar() {
       int eliminar=JOptionPane.showConfirmDialog(null, "Esta usted Seguro que quiere eliminar la asignatura?");
       if(eliminar==JOptionPane.YES_OPTION){
           AsignaturaFacade.getInstance().eliminar(asignatura);
           JOptionPane.showMessageDialog(null, "Asignatura Eliminada");
           this.dispose();
       }
    }

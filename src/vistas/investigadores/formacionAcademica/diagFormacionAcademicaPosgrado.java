    private void seleccionarCarreraPosgrado() {

        diagTituloPosgradoEleccion tituloPosgradoEleccion = new diagTituloPosgradoEleccion(null, true, tituloPosgrado);
        tituloPosgradoEleccion.setLocation(Comunes.centrarDialog(tituloPosgradoEleccion));
        tituloPosgradoEleccion.setVisible(true);
        if (tituloPosgradoEleccion.getTituloPosgradoEscogido() != null) {
            tituloPosgrado = tituloPosgradoEleccion.getTituloPosgradoEscogido();
            tfCarrera.setText(tituloPosgrado.toString());
        }

    }

    private void aceptar() {
        if (validar2()) {

            if (tipoOperacion.equals("Alta")) {
                formacionAcademicaPosgrado = new FormacionAcademicaPosgrado();
                formacionAcademicaPosgrado.setTituloPosgrado(tituloPosgrado);
                formacionAcademicaPosgrado.setUniversidad(universidad);
                if (!chkSDatos.isSelected()) {
                    anoIngreso = Integer.parseInt(tfAnoIngreso.getText());
                    if (chkCursandoActualmente.isSelected()) {
                        anoEgreso = 0;
                    } else {
                        anoEgreso = Integer.parseInt(tfAnoEgreso.getText());

                    }
                } else {
                    anoIngreso = 0;
                    anoEgreso = 0;
                }
                if (chkFinalizado.isSelected()) {
                    formacionAcademicaPosgrado.setFinalizado(true);
                } else {
                    formacionAcademicaPosgrado.setFinalizado(false);
                }

                formacionAcademicaPosgrado.setAnoIngreso(anoIngreso);
                formacionAcademicaPosgrado.setAnoEgreso(anoEgreso);                
                this.dispose();
            } else if (tipoOperacion.equals("Modificaci칩n")) {
                if (chkFinalizado.isSelected()) {
                    formacionAcademicaPosgrado.setFinalizado(true);
                } else {
                    formacionAcademicaPosgrado.setFinalizado(false);
                }
                tituloPosgrado.setDescripcion(tfCarrera.getText());
                TituloPosgradoFacade.getInstance().modificar(tituloPosgrado);
            }
        }
    }

    private boolean validar2() {
        boolean flag = false;
        if (chkSDatos.isSelected()) {
            if (!tfCarrera.getText().isEmpty()) {
                if (!tfUniversidad.getText().isEmpty()) {
                    flag = true;
                } else {
                    JOptionPane.showMessageDialog(null, "Debe escoger una universidad",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Debe escoger una carrera",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else if (!tfCarrera.getText().isEmpty()) {
            if (!tfUniversidad.getText().isEmpty()) {
                if (IntegerValidator.getInstance().isValid(tfAnoIngreso.getText())) {
                    if (chkCursandoActualmente.isSelected()) {
                        flag = true;
                    } else if (IntegerValidator.getInstance().isValid(tfAnoEgreso.getText())) {
                        flag = true;
                    } else {
                        JOptionPane.showMessageDialog(null, "A침o de egreso incorrecto",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "A침o de ingreso incorrecto",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe escoger una universidad",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger una carrera",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        return flag;
    }


    private void guardarRegistro() {
        
        RendicionDetalle rendicionDetalle = new RendicionDetalle();
        int cantidad = 0;
        if(!tfCantidad.getText().isEmpty()){
            cantidad = Integer.parseInt(tfCantidad.getText());
        }            
        rendicionDetalle.setCantidadCompra(cantidad);        
        rendicionDetalle.setDetalle(tfDetalle.getText());
        rendicionDetalle.setFechaCompra(dpFecha.getDate());
        rendicionDetalle.setNumeroActaPatrimonial(tfActaPatrimonial.getText());
        rendicionDetalle.setNumeroComprobante(tfNumeroComprobante.getText());
        if(!tfPrecio.getText().equals("")){
            rendicionDetalle.setPrecioUnitario(new BigDecimal(tfPrecio.getText()));
        }else{
            rendicionDetalle.setPrecioUnitario(new BigDecimal("0"));
        }
        rendicionDetalle.setTipoBien(cmbTipoBien.getSelectedItem().toString());
        
        listaRendicionDetalle.add(rendicionDetalle);
        setListaRendicionDetalle(listaRendicionDetalle);
        
        JOptionPane.showMessageDialog(null, "Datos agregados");
        limpiarCampos();
        //setRendicionDetalle(rendicionDetalle);
        this.dispose();
    }

    private void limpiarCampos() {
        tfActaPatrimonial.setText("");
        tfCantidad.setText("");
        tfPrecio.setText("");
        tfDetalle.setText("");
        tfProveedor.setText("");
        tfNumeroComprobante.setText("");
        taObservacion.setText("");
        tfActaPatrimonial.requestFocus();
    }

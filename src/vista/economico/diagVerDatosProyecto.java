    private void cargarDatosProyecto() {
            ParticipacionFacade parti = new ParticipacionFacade();
            
            lblCodigoProyecto.setForeground(Color.blue);
            lblNombreProyecto.setForeground(Color.blue);
            lblDuracion.setForeground(Color.blue);
            lblFechaFin.setForeground(Color.blue);
            lblFechaInicio.setForeground(Color.blue);
            lblUnidadAcademica.setForeground(Color.blue);
            lblDirector.setForeground(Color.blue);
            lblProrroga.setForeground(Color.blue);
            lblUnidadEjecutora.setForeground(Color.blue);
            lblTipoProyecto.setForeground(Color.blue);
            lblLineaInvestigacion.setForeground(Color.blue);
            lblEntidadEvaluadora.setForeground(Color.blue);
            
            lblCodigoProyecto.setText(getProyectoSeleccionado().getCodigoIncentivos());
            lblNombreProyecto.setText(getProyectoSeleccionado().getTitulo());
            lblFechaInicio.setText(formato.format(getProyectoSeleccionado().getFechaInicio()));
            lblFechaFin.setText(formato.format(getProyectoSeleccionado().getFechaFinalizacion()));
            lblDuracion.setText(String.valueOf(getProyectoSeleccionado().getDuracionTeorica()));
            try {
                lblUnidadEjecutora.setText(getProyectoSeleccionado().getUnidadEjecutora().getDescripcion());
            } catch (java.lang.NullPointerException ex){
                lblUnidadEjecutora.setText("-");
            }
            
            try {
                lblTipoProyecto.setText(getProyectoSeleccionado().getTipoProyecto().getDescripcion());
            } catch (java.lang.NullPointerException ex){
                lblTipoProyecto.setText("-");
            }
            
            try {
                lblLineaInvestigacion.setText(getProyectoSeleccionado().getLineaInvestigacion().getDescripcion());
            }catch (java.lang.NullPointerException ex){
                lblLineaInvestigacion.setText("-");
            }
            
            try {
                lblEntidadEvaluadora.setText(getProyectoSeleccionado().getEntidadEvaluadora().getDescripcion());
            }catch(java.lang.NullPointerException ex){
                lblEntidadEvaluadora.setText("-");
            }
                        
            try {
                lblUnidadAcademica.setText(getProyectoSeleccionado().getUnidadAcademica().getDescripcion());
            } catch (java.lang.NullPointerException ex) {
                lblUnidadAcademica.setText("-");
            }
            
            List<Prorroga> listaProrrogas = proyectoSeleccionado.getProrrogas();
            if(listaProrrogas.size() > 0){
                lblProrroga.setText("SI");
                Comunes.cargarJList(lstProrrogas, listaProrrogas);
            }else{
                lblProrroga.setText("NO");
            }
            
            
            p = parti.getDirector(getProyectoSeleccionado());
            if (p == null) {
                lblDirector.setText("-");
            } else {
                lblDirector.setText(p.getInvestigador().getPersona().getApellido()
                        + " " + p.getInvestigador().getPersona().getNombre());
            }
            
            try{
                Comunes.cargarJList(lstIntegrantes, proyectoSeleccionado.getParticipaciones());
            }catch(java.lang.NullPointerException ex){
                
            }
    }


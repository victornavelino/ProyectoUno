    private void ingresarCantidad(int anios) {
        if (anios > 5 || anios < 1) {
            JOptionPane.showMessageDialog(null, "La cantidad ingresada no "
                    + "puede ser menor que 1 ni mayor que 5");
        } else {            
            switch (anios) {
                case 1:
                    gestionarComponentes(true, true, false, false, false, false);
                    break;
                case 2:                    
                    gestionarComponentes(true, true, true, false, false, false);
                    break;
                case 3:
                    //gestionarComponentes(true, true, true, true, false);
                    JOptionPane.showMessageDialog(null, "Un proyecto no puede "
                            + "tener una duracin de 3 aos");
                    break;
                case 4:
                    gestionarComponentes(true, true, true, true, true, false);
                    break;
                case 5:
                    gestionarComponentes(true, true, true, true, true, true);
                    break;
            };
            lblIngreso.setVisible(false);
            btnAceptar.setVisible(false);
            tfAnios.setVisible(false);
        }
    }


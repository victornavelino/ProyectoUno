    private void guardarRegistro() {
        PagoEconomico pago = new PagoEconomico();               
        ResolucionConsejoSuperior resolucionConsejoSuperior = new ResolucionConsejoSuperior();
        ResolucionRectoral resolucionRectoral = new ResolucionRectoral();
        ResolucionSecyt resolucionSecyt = new ResolucionSecyt();
        Documento resoCS = new Documento();
        Documento resoR = new Documento();
        Documento resoS = new Documento();
               
        //resolucionConsejoSuperior.setNumero(Long.parseLong(tfResolucionCS.getText()));
        resolucionConsejoSuperior.setNumero(
                Comunes.validarStringALong(tfResolucionCS.getText()));        
        resolucionConsejoSuperior.setAnio(
                Comunes.validarStringAInt(tfAnioCS.getText()));
        if(!tfResolucionCS.getText().equals("")){
            resolucionConsejoSuperior.setFechaResolucion(dpFechaCS.getDate());
        }else{
            resolucionConsejoSuperior.setFechaResolucion(null);
        }
        
        resolucionRectoral.setNumero(
                Comunes.validarStringALong(tfResolucionR.getText()));
        resolucionRectoral.setAnio(
                Comunes.validarStringAInt(tfAnioR.getText()));
        if(!tfResolucionR.getText().equals("")){
            resolucionRectoral.setFechaResolucion(dpFechaR.getDate());
        }else{
            resolucionRectoral.setFechaResolucion(null);
        }
        
        resolucionSecyt.setNumero(
                Comunes.validarStringALong(tfResolucionSecyt.getText()));
        resolucionSecyt.setAnio(
                Comunes.validarStringAInt(tfAnioSecyt.getText()));
        if(!tfResolucionSecyt.getText().equals("")){
            resolucionSecyt.setFechaResolucion(dpFechaSecyt.getDate());               
        }else{
            resolucionSecyt.setFechaResolucion(null);
        }
        
        if(!tfArchivoCS.getText().equals("")){
            resoCS = guardarArchivoAdjunto(tfArchivoCS.getText());        
        }else{
            resoCS = null;
        }
        if(!tfArchivoR.getText().equals("")){
            resoR = guardarArchivoAdjunto(tfArchivoR.getText());
        }else{
            resoR = null;
        }
        if(!tfArchivoS.getText().equals("")){
            resoS = guardarArchivoAdjunto(tfArchivoS.getText());
        }else{
            resoS = null;
        }
        pago.setArchivoResolucionConsejoSuperior(resoCS);
        pago.setArchivoResolucionRectoral(resoR);
        pago.setArchivoResolucionSecyt(resoS);
        
        pago.setNumeroExpediente(tfNumeroExpediente.getText());        
        pago.setAnioExpediente(
                Comunes.validarStringAInt(tfAnioExpediente.getText()));        
        if(Comunes.validarBigDecimal(tfMonto.getText())){
            pago.setMonto(new BigDecimal(tfMonto.getText()));              
        }else{        
            pago.setMonto(new BigDecimal("0"));              
        }
        pago.setResolucionConsejoSuperior(resolucionConsejoSuperior);        
        pago.setResolucionRectoral(resolucionRectoral);
        pago.setResolucionSecyt(resolucionSecyt);                
        pago.setObservacion(taObservacion.getText());
        List<PagoEconomico> listaPagoAnteriores =  proyectoSeleccionado.getPagos();
        
        ArrayList al = new ArrayList();
        for(PagoEconomico p: listaPagoAnteriores){
            al.add(p);
        }
        al.add(pago);
        proyectoSeleccionado.setPagos(al);
        
        facade.ResolucionRectoralFacade.getInstance().altaResolucionRectoral(resolucionRectoral);
        facade.ResolucionConsejoSuperiorFacade.getInstance().altaResolucionConsejoSuperior(resolucionConsejoSuperior);
        facade.ResolucionSecytFacade.getInstance().altaResolucionSecyt(resolucionSecyt);
        facade.PagoEconomicoFacade.getInstance().altaPago(pago);
        
        Operacion operacion=new Operacion();
        operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
        operacion.setOperacion("Alta de Resoluciones y pagos (Economico)");
        operacion.setUsuario(usuario);
        OperacionFacade.getInstance().alta(operacion);
        
        facade.ProyectoFacade.getInstance().modificar(proyectoSeleccionado);
        
        JOptionPane.showMessageDialog(null, "Datos Guardados");
        this.dispose();
    }


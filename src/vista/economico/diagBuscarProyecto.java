    private void limpiarComponentes() {
        tfTexto.setText("");     
        manipularComponentes(false);        
    }
    
    private void manipularComponentes(boolean valor){
        btnSeleccionar.setEnabled(valor);
    }
    
    private void habilitarComponentes() {
        manipularComponentes(true);
    }
        
    private void inicializarComponetes() {
        this.setLocationRelativeTo(null);
        getRootPane().setDefaultButton(btnBuscar);
        ButtonGroup grupoDeBotones = new ButtonGroup();
        grupoDeBotones.add(rbtnProyecto);
        grupoDeBotones.add(rbtnDirector);
        rbtnDirector.setSelected(true); 
        tfTexto.requestFocus();
        limpiarComponentes();
    }
    
    private void buscarProyecto() {        
        List<Proyecto> listaProyecto = null;
        if(!tfTexto.getText().isEmpty()){
            if(rbtnProyecto.isSelected()){
                listaProyecto = facade.ProyectoFacade.getInstance().
                    buscar(tfTexto.getText());
            }else{
                listaProyecto = facade.ProyectoFacade.getInstance().
                    ProyectoxDirector(tfTexto.getText().toUpperCase(),"Director");
            }
        }else{
            listaProyecto = facade.ProyectoFacade.getInstance().getTodos();
        }
        if(listaProyecto.isEmpty()){
            JOptionPane.showMessageDialog(null, "No se encontraron resultados "
                    + "para: " + tfTexto.getText());
        }
        Comunes.cargarJList(lstResultado, listaProyecto);
        limpiarComponentes();
    }

    private void seleccionarProyecto() {        
        setProyectoSeleccionado((Proyecto) lstResultado.getSelectedValue()); 
        this.dispose();
    }

   
    

    private void inicializarComponentes() {
        this.setLocationRelativeTo(null);
        getRootPane().setDefaultButton(btnGuardar);
        btnImprimir.setVisible(false);        
        habilitarComponentes(false); 
        btnAgregarDetalleRendicion.setVisible(false);
        btnEliminarDetalleRendicion.setVisible(false);
        cargarComponentes();       
        if(getRendicion().getRendicionDetalle().size() > 0){
            listaRendicionesDetalle = getRendicion().getRendicionDetalle();   
            Collections.sort(listaRendicionesDetalle);
            calcularTotalesRendicion(listaRendicionesDetalle);
        }
    }
    

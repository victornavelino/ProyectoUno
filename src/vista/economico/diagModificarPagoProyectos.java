    private void guardarRegistro() {
        PagoEconomico pagoModificado = getPago();
        ResolucionConsejoSuperior resolucionConsejoSuperior = getPago().getResolucionConsejoSuperior();
        ResolucionRectoral resolucionRectoral = getPago().getResolucionRectoral();
        ResolucionSecyt resolucionSecyt = getPago().getResolucionSecyt();
        Documento ResoCS;
        Documento ResoR;
        Documento ResoS;

        resolucionConsejoSuperior.setNumero(
                Comunes.validarStringALong(tfResolucionCS.getText()));
        resolucionConsejoSuperior.setAnio(
                Comunes.validarStringAInt(tfAnioResolucionCS.getText()));
        resolucionConsejoSuperior.setFechaResolucion(dpFechaResolucionCS.getDate());
        resolucionRectoral.setNumero(
                Comunes.validarStringALong(tfResolucionR.getText()));
        resolucionRectoral.setAnio(
                Comunes.validarStringAInt(tfAnioResolucionR.getText()));
        resolucionRectoral.setFechaResolucion(dpFechaResolucionR.getDate());
        resolucionSecyt.setNumero(
                Comunes.validarStringALong(tfResolucionSecyt.getText()));
        resolucionSecyt.setAnio(
                Comunes.validarStringAInt(tfAnioResolucionSecyt.getText()));
        resolucionSecyt.setFechaResolucion(dpFechaResolucionSecyt.getDate());

        pagoModificado.setNumeroExpediente(tfNumeroExpediente.getText());
        pagoModificado.setAnioExpediente(
                Comunes.validarStringAInt(tfAnioExpediente.getText()));
        if(Comunes.validarBigDecimal(tfMonto.getText())){
            pagoModificado.setMonto(new BigDecimal(tfMonto.getText()));
        }else{
            pagoModificado.setMonto(new BigDecimal("0"));
        }
        
        if(tfArchivoCS.getText().equals("")){
            pagoModificado.setArchivoResolucionConsejoSuperior(null);
        }else{
            if(modificadoCS){
                ResoCS = guardarArchivoAdjunto(tfArchivoCS.getText());
                pagoModificado.setArchivoResolucionConsejoSuperior(ResoCS);
            }            
        }
        
        if(tfArchivoR.getText().equals("")){
            pagoModificado.setArchivoResolucionRectoral(null);
        }else{
            if(modificadoR){
                ResoR = guardarArchivoAdjunto(tfArchivoR.getText());
                pagoModificado.setArchivoResolucionRectoral(ResoR);
            }
        }
        
        if(tfArchivoS.getText().equals("")){
            pagoModificado.setArchivoResolucionSecyt(null);
        }else{
            if(modificadoS){
                ResoS = guardarArchivoAdjunto(tfArchivoS.getText());
                pagoModificado.setArchivoResolucionSecyt(ResoS);
            }            
        }
        
        pagoModificado.setResolucionConsejoSuperior(resolucionConsejoSuperior);
        pagoModificado.setResolucionRectoral(resolucionRectoral);
        pagoModificado.setResolucionSecyt(resolucionSecyt);
        pagoModificado.setObservacion(taObservacion.getText());

        facade.ResolucionRectoralFacade.getInstance().modificacionResolucionRectoral(resolucionRectoral);
        facade.ResolucionConsejoSuperiorFacade.getInstance().modificacionResolucionConsejoSuperior(resolucionConsejoSuperior);
        facade.ResolucionSecytFacade.getInstance().modificacionResolucionSecyt(resolucionSecyt);
        facade.PagoEconomicoFacade.getInstance().modificarPago(pagoModificado);
        
        Operacion operacion = new Operacion();
        operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
        operacion.setOperacion("Modificación de Pago (Economico)");
        operacion.setUsuario(usuario);
        OperacionFacade.getInstance().alta(operacion);

        JOptionPane.showMessageDialog(null, "Datos Guardados");
        this.dispose();
    }


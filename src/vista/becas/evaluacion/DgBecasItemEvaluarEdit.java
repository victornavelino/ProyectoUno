    private void action(){
        
        switch(iTipoOp){
            case 0:
                 this.guardar();
                break;
            case 1:
                this.modificar();
                break;
            case 2:
                this.eliminar();
                break;
        }//fin switch
    }//fin action
    
    private void guardar(){
        String sMensaje = "";
        int iTipoMsg = JOptionPane.INFORMATION_MESSAGE;
        try{
            
            becasItemEvaluar = new BecasItemEvaluar();
            becasItemEvaluar.setDescripcion(jtaDescripcion.getText());
            
            this.validar(becasItemEvaluar, 0);
            
            BecasItemEvaluarFacade.getInstance().alta(becasItemEvaluar);
            sMensaje = "El dato fue guardado";
            this.setRstOperacion(Boolean.TRUE);
        }catch(Exception e){
            this.setRstOperacion(Boolean.FALSE);
            sMensaje = "Error: " + e.getMessage();
            iTipoMsg = JOptionPane.ERROR_MESSAGE;
        }finally{
            JOptionPane.showMessageDialog(this,sMensaje, "Mensaje", iTipoMsg);
        }
    }//fin guardar
    
    private void modificar(){
        String sMensaje = "";
        int iTipoMsg = JOptionPane.INFORMATION_MESSAGE;
        try{
            becasItemEvaluar = new BecasItemEvaluar();
            becasItemEvaluar.setId(Long.parseLong(jtfId.getText()));
            becasItemEvaluar.setDescripcion(jtaDescripcion.getText());
            
            this.validar(becasItemEvaluar, 1);
            
            BecasItemEvaluarFacade.getInstance().modificar(becasItemEvaluar);
            sMensaje = "El dato fue modificado";
            this.setRstOperacion(Boolean.TRUE);
        }catch(Exception e){
            
            this.setRstOperacion(Boolean.FALSE);
            sMensaje = "Error: " + e.getMessage();
            iTipoMsg = JOptionPane.ERROR_MESSAGE;
        }finally{
            JOptionPane.showMessageDialog(this,sMensaje, "Mensaje", iTipoMsg);
        }
    }//fin modificar
    
    private void eliminar(){
        String sMensaje = "";
        int iTipoMsg = JOptionPane.INFORMATION_MESSAGE;
        try{
            becasItemEvaluar = new BecasItemEvaluar();
            becasItemEvaluar.setId(Long.parseLong(jtfId.getText()));     
            becasItemEvaluar.setDescripcion(jtaDescripcion.getText());
            
            BecasItemEvaluarFacade.getInstance().eliminar(becasItemEvaluar);
            sMensaje = "El dato fue eliminado";
            this.setRstOperacion(Boolean.TRUE);
        }catch(Exception e){
            this.setRstOperacion(Boolean.FALSE);
            sMensaje = "Error: " + e.getMessage();
            iTipoMsg = JOptionPane.ERROR_MESSAGE;
        }finally{
            JOptionPane.showMessageDialog(this,sMensaje, "Mensaje", iTipoMsg);
            if(this.getRstOperacion()){
                this.setVisible(Boolean.FALSE);
            }
        }
    }//fin eliminar
    
    public void cargarCampos(BecasItemEvaluar becasItemEvaluar, int op){
        
        switch(op){
            case 0:
                this.sLabelButton = "Guardar";
                break;
            case 1:
                 this.sLabelButton = "Modificar";
                 this.jtfId.setText(String.valueOf(becasItemEvaluar.getId()));
                 this.jtaDescripcion.setText(becasItemEvaluar.getDescripcion());
                break;
            case 2:
                this.sLabelButton = "Eliminar";
                this.jtfId.setText(String.valueOf(becasItemEvaluar.getId()));
                this.jtaDescripcion.setText(becasItemEvaluar.getDescripcion());
                break;
                
        }//fin switch
        
        iTipoOp = op;
        this.jbAction.setText(sLabelButton);
        
    }//fin 
    
    private void validar(BecasItemEvaluar um, int op) throws Exception{
        if(um.getDescripcion().isEmpty()){
            throw new Exception("No ingreso la descripcion.");
        }//fin if
        
        if(BecasItemEvaluarFacade.getInstance().bFindByNombre(um.getDescripcion(), um.getId(), op)){
            throw new Exception("Ya existe un item con el nombre ingresado.");
        }//fin if
    }
    

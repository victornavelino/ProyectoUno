    private void inicializarComponentes() {
        cargarListaCargo();
        jListCargo.requestFocus();
    }

    private void cargarListaCargo() {
        List<Cargo> listaCargo = CargoFacade.getInstance().listarTodosCargoOrdenados();
        DefaultListModel modeloLista = new DefaultListModel();
        for (Cargo cargo : listaCargo) {
            modeloLista.addElement(cargo);
        }
        jListCargo.setModel(modeloLista);
    }

    private void agregar() {
        diagCargoAlta cargoAlta = new diagCargoAlta(null, true);
        cargoAlta.setLocation(Comunes.centrarDialog(cargoAlta));
        cargoAlta.setVisible(true);
        inicializarComponentes();
    }

    private void modificar() {
        if (jListCargo.getSelectedIndex() != -1) {
            diagCargoModificacion cargoModificacion = new diagCargoModificacion(null, true, (Cargo) jListCargo.getSelectedValue());
            cargoModificacion.setLocation(Comunes.centrarDialog(cargoModificacion));
            cargoModificacion.setVisible(true);
            inicializarComponentes();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un cargo", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

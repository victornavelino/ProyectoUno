    private void inicializarComponentes() {
        cargarListaUnidadEjecutora();
        jListUnidadEjecutora.requestFocus();
    }

    private void cargarListaUnidadEjecutora() {
        List<UnidadEjecutora> listaUnidadEjecutora = unidadEjecutoraFacade.getTodosUnidadEjecutora();
        DefaultListModel modeloLista = new DefaultListModel();
        for (UnidadEjecutora unidadEjecutora : listaUnidadEjecutora) {
            modeloLista.addElement(unidadEjecutora);
        }
        jListUnidadEjecutora.setModel(modeloLista);
    }

    private void agregar() {
        diagUnidadEjecutoraAlta unidadEjecutoraAlta = new diagUnidadEjecutoraAlta(null, true);
        unidadEjecutoraAlta.setLocation(Comunes.centrarDialog(unidadEjecutoraAlta));
        unidadEjecutoraAlta.setVisible(true);
        inicializarComponentes();
    }


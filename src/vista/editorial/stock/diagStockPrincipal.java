    private void inicializarComponentes() {
        cargarListadoStock();
        cargarFormatos();
    }

    private void cargarListadoStock() {
        Comunes.cargarJList(jListStock, StockFacade.getInstance().listarTodos());
    }
     private void filtrarPorTitulo() {
        if (!tfFiltroTitulo.getText().equals("")) {
            Comunes.cargarJList(jListStock, StockFacade.getInstance().buscarPorPublicacion(tfFiltroTitulo.getText()));
        } else {
            cargarListadoStock();
        }
    }


    private void filtrarPorAutor() {
        if (!tfFiltroAutor.getText().equals("")) {
            Comunes.cargarJList(jListStock, StockFacade.getInstance().buscarPorPublicacionAutor(tfFiltroAutor.getText()));
        } else {
            cargarListadoStock();
        }   
    }

    private void filtrarPorFormato() {
        if (cboFormato.getSelectedIndex() != -1) {
            Comunes.cargarJList(jListStock, StockFacade.getInstance().buscarPorFormato((FormatoLibro) cboFormato.getSelectedItem()));
        } else {
            cargarListadoStock();
        }       }

    private void filtrarPorISBN() {
if (!tfFiltroISBN.getText().equals("")) {
            Comunes.cargarJList(jListStock, StockFacade.getInstance().buscarPorPublicacionISBN(tfFiltroISBN.getText()));
        } else {
            cargarListadoStock();
        }        }
    private void cargarFormatos(){
        Comunes.cargarJCombo(cboFormato, FormatoFacade.getInstance().listarTodos());
    }


    private void agregarStock() {
        
        diagStock   stock = new diagStock(null, true, usuario, 
                   "Alta", "Stock");

        
        stock.setLocation(Comunes.centrarDialog(stock));
        stock.setVisible(true);
        if (stock.getStockCreado() != null) {
            cargarListadoStock();
        }
        cargarFormatos();
    }

    private void eliminarStock() {
        if (jListStock.getSelectedIndex() != -1) {
            int i = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea eliminar?", "Confirmar", JOptionPane.YES_NO_CANCEL_OPTION);
            if (i == JOptionPane.YES_OPTION) {
                Stock stock =  (Stock) jListStock.getSelectedValue();
                StockFacade.getInstance().Eliminar(stock.getId());
                JOptionPane.showMessageDialog(null, "Unidad Eliminada.");
                cargarListadoStock();

            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila.");
        }
    }    

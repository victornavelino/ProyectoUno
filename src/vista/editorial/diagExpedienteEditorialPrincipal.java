    private void cargarExpedientesJList() {
        Comunes.cargarJList(jListExpedientes, ExpedienteEditorialFacade.getInstance().listarTodos());
    }
    
      private void modificarExpediente() {
        if (jListExpedientes.getSelectedIndex() != -1) {
            modificarExpedienteSeleccionado();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger una publicación", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void modificarExpedienteSeleccionado() {
        ExpedienteEditorial exp = (ExpedienteEditorial) jListExpedientes.getSelectedValue();
        diagExpedienteEditorial expediente = new diagExpedienteEditorial(null, true,exp, exp.getPublicacion(),  usuario, "Modificación");
        expediente.setLocation(Comunes.centrarDialog(expediente));
        expediente.setVisible(true);
        cargarExpedientesJList();
    }

    private void eliminarExpediente() {
        if (jListExpedientes.getSelectedIndex() != -1) {
            ExpedienteEditorial p = (ExpedienteEditorial) jListExpedientes.getSelectedValue();
            Object[] opciones = {"Aceptar", "Cancelar"};
            JOptionPane nombreDelDialogo = new JOptionPane();

            int eleccion = JOptionPane.showOptionDialog(this,
                    "Esta seguro que quiere eliminar el expediente con el codigo:" + p.getCodigo(),
                    "Confirmar Eliminación",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    opciones,
                    "Aceptar");

            if (eleccion == JOptionPane.YES_OPTION) {

                ExpedienteEditorialFacade.getInstance().eliminar((ExpedienteEditorial) jListExpedientes.getSelectedValue());
                cargarExpedientesJList();

            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un expediente", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void filtrarPorCodigo() {
        if (!tfFiltroCodigo.getText().equals("")) {
            Comunes.cargarJList(jListExpedientes, ExpedienteEditorialFacade.getInstance().buscarPorCodigo(tfFiltroCodigo.getText()));
        } else {
            cargarExpedientesJList();
        }
    }

    private void filtrarPorPublicacion() {
        if (!tfFiltroPublicacion.getText().equals("")) {
            Comunes.cargarJList(jListExpedientes, ExpedienteEditorialFacade.getInstance().buscarPorPublicacion(tfFiltroPublicacion.getText()));
        } else {
            cargarExpedientesJList();
        }
    }

    private void filtrarPorIniciador() {
        if (!tfFiltroIniciador.getText().equals("")) {
            Comunes.cargarJList(jListExpedientes, ExpedienteEditorialFacade.getInstance().buscarPorIniciador(tfFiltroIniciador.getText()));
        } else {
            cargarExpedientesJList();
        }
    }

  

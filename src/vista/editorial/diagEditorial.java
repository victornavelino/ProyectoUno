    private void guardar() {
        if (validar()) {
            if (tipoOperacion.equals("Alta")) {
                editorialCientifica = new EditorialCientifica();
                asignarDatos();
                altaEditorial();
                altaStock();
                diagExpedienteEditorial expedienteEditorial;
                expedienteEditorial = new diagExpedienteEditorial(null, true, editorialCientifica, usuario, "Alta");
                expedienteEditorial.setLocation(Comunes.centrarDialog(expedienteEditorial));
                expedienteEditorial.setVisible(true);
                this.dispose();
            } else if (tipoOperacion.equals("Modificacin")) {
                asignarDatos();
                modificarEditorial();
                this.dispose();
            }
        }
    }

    private boolean validar() {
        boolean flag = false;
        if (!jtfTitulo.getText().equals("")) {            
            if (!jtfIsbn.getText().equals("")) {

                flag = true;
            } else {
                JOptionPane.showMessageDialog(null, "Debe ingresar el ISBN de la publicacin", "Error", JOptionPane.ERROR_MESSAGE);
            }            
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar el ttulo de la publicacin", "Error", JOptionPane.ERROR_MESSAGE);
        }
        jtfTitulo.requestFocus();
        return flag;
    }


    private void guardarFormato() {
        if(tfFormato.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar un formato");
        }else{
            FormatoEditorial formato = new FormatoEditorial();
            
            formato.setFormato(tfFormato.getText());
            
            FormatoEditorialFacade.getInstance().alta(formato);
            
            Operacion operacion = new Operacion();
            operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
            operacion.setOperacion("Alta de formato");
            operacion.setUsuario(usuario);
            OperacionFacade.getInstance().alta(operacion);
            
            JOptionPane.showMessageDialog(null, "Formato agregado con Ã©xito!!");
        }
    }

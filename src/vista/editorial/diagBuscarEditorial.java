    private void filtrarPorISBN() {
        if (!tfFiltroISBN.getText().equals("")) {
            if (!EditorialCientificaFacade.getInstance().buscarPorISBN(tfFiltroISBN.getText()).isEmpty()) {
                Comunes.cargarJList(jListEditoriales, EditorialCientificaFacade.getInstance().buscarPorISBN(tfFiltroISBN.getText()));

            }
            else{
                JOptionPane.showMessageDialog(null, "No existen resultados para ISBN: '"+tfFiltroISBN.getText()+"'", "Error", JOptionPane.ERROR_MESSAGE);

            }
        } else {
            cargarEditorialesJList();
        }
    }

    private void escogerEditorial() {
        if(jListEditoriales.getSelectedIndex() != -1) {
            editorial = (EditorialCientifica) jListEditoriales.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger una publicación de la lista",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }

    public EditorialCientifica getEditorial() {
        return this.editorial;
    }
  private void filtrarPorTitulo() {
        if (!tfFiltroTitulo.getText().equals("")) {
            Comunes.cargarJList(jListEditoriales, EditorialCientificaFacade.getInstance().buscarPorTitulo(tfFiltroTitulo.getText()));
        } else {
            cargarEditorialesJList();
        }
    }

    private void filtrarPorAutor() {
        if (!tfFiltroAutor.getText().equals("")) {
            Comunes.cargarJList(jListEditoriales, EditorialCientificaFacade.getInstance().buscarPorAutor(tfFiltroAutor.getText()));
        } else {
            cargarEditorialesJList();
        }
    }

    private void cargarEditorialesJList() {
            Comunes.cargarJList(jListEditoriales, EditorialCientificaFacade.getInstance().getTodosEditorial());
    }


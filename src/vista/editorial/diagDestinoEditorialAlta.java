    private void guardarDestino() {
        if(tfDestino.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar un destino");
        }else{
            DestinoEditorial destino = new DestinoEditorial();
            
            destino.setNombre(tfDestino.getText());
            
            DestinoEditorialFacade.getInstance().alta(destino);
            
            Operacion operacion = new Operacion();
            operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
            operacion.setOperacion("Alta de destino de donación");
            operacion.setUsuario(usuario);
            OperacionFacade.getInstance().alta(operacion);
            
            JOptionPane.showMessageDialog(null, "Destino agregado con éxito!!");
        }
    }

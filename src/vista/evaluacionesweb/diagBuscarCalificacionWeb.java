    private void inicializarComponentes() {
        Comunes.cargarJList(jlistCalificacionWeb, CalificacionWebFacade.getInstance().getTodos());
    }

    private void aceptar() {
        if (!jlistCalificacionWeb.getSelectedValuesList().isEmpty()) {
            listaCalificacionWeb = jlistCalificacionWeb.getSelectedValuesList();
            this.dispose();
        }
    }

    private void buscarCalificacion() {
        if (!tfDescripcion.getText().isEmpty()) {
            Comunes.cargarJList(jlistCalificacionWeb, CalificacionWebFacade.getInstance().getCalificaciones(tfDescripcion.getText()));
        } else {
            Comunes.cargarJList(jlistCalificacionWeb, CalificacionWebFacade.getInstance().getTodos());
        }
    }

    private void agregarNuevaCalificacion() {
        DiagCalificacion diagCalificacion = new DiagCalificacion(null, true, "Alta");
        diagCalificacion.setLocation(Comunes.centrarDialog(diagCalificacion));
        diagCalificacion.setVisible(true);
        Comunes.cargarJList(jlistCalificacionWeb, CalificacionWebFacade.getInstance().getTodos());
    }

    private void modificarCalificacion() {
        DiagCalificacion diagCalificacion = new DiagCalificacion(null, true, "Modificaci√≥n", (CalificacionWeb) jlistCalificacionWeb.getSelectedValue());
        diagCalificacion.setLocation(Comunes.centrarDialog(diagCalificacion));
        diagCalificacion.setVisible(true);
        Comunes.cargarJList(jlistCalificacionWeb, CalificacionWebFacade.getInstance().getTodos());
    }

    private void eliminar() {
        int m = JOptionPane.showConfirmDialog(null, "Desea eliminar la calificacion seleccionada?", "Confirmar eliminacion", JOptionPane.YES_NO_OPTION);
        if (m == 0) {
            try {
                CalificacionWebFacade.getInstance().eliminar((CalificacionWeb) jlistCalificacionWeb.getSelectedValue());
                Comunes.cargarJList(jlistCalificacionWeb, CalificacionWebFacade.getInstance().getTodos());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "No se puede eliminar, la calificacion esta siendo utilizada \n"
                        + "primero debe eliminar las evaluaciones asociadas");
            }

        }
    }

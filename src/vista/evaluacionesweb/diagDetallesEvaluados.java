    private void inicializarComponentes() {
     Comunes.cargarJList(jListContenidoBLoque, ContenidoBloqueFacade.getInstance().getTodos(bloque));
    }



    private void aceptar() {
        if (validar()) {
            detalleEvaluado = new DetalleEvaluado();
            detalleEvaluado.setContenidoBloque((ContenidoBloque)jListContenidoBLoque.getSelectedValue());
            DetalleEvaluadoFacade.getInstance().alta(detalleEvaluado);
            this.dispose();
        }

    }





    private boolean validar() {
           if(jListContenidoBLoque.getSelectedIndex()==-1){
               JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la lista");
               return false;
           }
        return true;

    }

    private void agregarContenidoBloque() {
        diagContenidoBloque diagDetalleEvaluado = new diagContenidoBloque(null, rootPaneCheckingEnabled, bloque);
        diagDetalleEvaluado.setLocation(Comunes.centrarDialog(diagDetalleEvaluado));
        diagDetalleEvaluado.setVisible(true);
        if(diagDetalleEvaluado.getDetalleEvaluado()!=null){
            Comunes.cargarJList(jListContenidoBLoque, ContenidoBloqueFacade.getInstance().getTodos(bloque));
        }
    }

    private void eliminar() {
        if(jListContenidoBLoque.getSelectedIndex()!=-1){
            ContenidoBloqueFacade.getInstance().eliminar((ContenidoBloque)jListContenidoBLoque.getSelectedValue());
            Comunes.cargarJList(jListContenidoBLoque, ContenidoBloqueFacade.getInstance().getTodos(bloque));
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la lista");
        }
         }

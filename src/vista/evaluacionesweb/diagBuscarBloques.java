    private void inicializarComponentes() {
        Comunes.cargarJList(jlistBloques, BloqueFacade.getInstance().getTodos());
    }

    private void aceptar() {
        if (!jlistBloques.getSelectedValuesList().isEmpty()) {
            listaBloques = jlistBloques.getSelectedValuesList();
            this.dispose();
        }
    }

    private void buscarBloques() {
        if (!tfDescripcion.getText().isEmpty()) {
            Comunes.cargarJList(jlistBloques, BloqueFacade.getInstance().getBloques(tfDescripcion.getText()));
        } else {
            Comunes.cargarJList(jlistBloques, BloqueFacade.getInstance().getTodos());
        }
    }

    private void agregarNuevoBloque() {
        diagNuevoBloque nuevoBloque=new diagNuevoBloque(null, true);
        nuevoBloque.setLocation(Comunes.centrarDialog(nuevoBloque));
        nuevoBloque.setVisible(true);
        Comunes.cargarJList(jlistBloques, BloqueFacade.getInstance().getTodos());
    }

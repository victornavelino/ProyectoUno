    private void inicializarComponentes() {
        Comunes.cargarJCombo(cboBloques, BloqueFacade.getInstance().getTodos());
        listaDetalleEvaluados = DetalleEvaluadoFacade.getInstance().getTodos((Bloque) cboBloques.getSelectedItem());
        Comunes.cargarJList(jllistDetallesEvaluados, listaDetalleEvaluados);
    }

    private void aceptar() {
        if (validar()) {
            bloqueEvaluado = new BloqueEvaluado();
            bloqueEvaluado.setBloque((Bloque) cboBloques.getSelectedItem());
            bloqueEvaluado.setDetallesEvaluados(listaDetalleEvaluados);
            BloqueEvaluadoFacade.getInstance().alta(bloqueEvaluado);
            this.dispose();
        }

    }

    private void quitarDetalleEvaluado() {
        if (jllistDetallesEvaluados.getSelectedIndex() != -1) {
            listaDetalleEvaluados.remove((DetalleEvaluado) jllistDetallesEvaluados.getSelectedValue());
            DetalleEvaluadoFacade.getInstance().eliminar((DetalleEvaluado) jllistDetallesEvaluados.getSelectedValue());
            Comunes.cargarJList(jllistDetallesEvaluados, listaDetalleEvaluados);
        }
    }

    private boolean validar() {
        if (listaDetalleEvaluados.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe cargar detalles");
        }
        return true;

    }

    private void agregarDetalleEvaluado() {
        diagDetallesEvaluados diagDetallesEvaluados=new diagDetallesEvaluados(null, true,  (Bloque) cboBloques.getSelectedItem());
        diagDetallesEvaluados.setLocation(Comunes.centrarDialog(diagDetallesEvaluados));
        diagDetallesEvaluados.setVisible(true);
       if (diagDetallesEvaluados.getDetalleEvaluado() != null) {
            listaDetalleEvaluados.add(diagDetallesEvaluados.getDetalleEvaluado());
            Comunes.cargarJList(jllistDetallesEvaluados, listaDetalleEvaluados);
        }
    }

    private void agregarBloque() {
        diagBuscarBloques buscarBloques = new diagBuscarBloques(null, true);
        buscarBloques.setLocation(Comunes.centrarDialog(buscarBloques));
        buscarBloques.setVisible(true);
        Comunes.cargarJCombo(cboBloques, BloqueFacade.getInstance().getTodos());
    }

    private void cargarDetallesEvaluadosSegunBloque() {
        listaDetalleEvaluados = DetalleEvaluadoFacade.getInstance().getTodos((Bloque) cboBloques.getSelectedItem());
        Comunes.cargarJList(jllistDetallesEvaluados, listaDetalleEvaluados);
    }

    private void aceptar() {
        if (validar()) {
            detalleEvaluado = new DetalleEvaluado();
            contenidoBloque=new ContenidoBloque();
            contenidoBloque.setDescripcion(taContenidoBloque.getText());
            contenidoBloque.setBloque(bloque);
            ContenidoBloqueFacade.getInstance().alta(contenidoBloque);
            detalleEvaluado.setContenidoBloque(contenidoBloque);
            DetalleEvaluadoFacade.getInstance().alta(detalleEvaluado);
            this.dispose();
        }

    }





    private boolean validar() {
           if(taContenidoBloque.getText().isEmpty()){
               JOptionPane.showMessageDialog(null, "Debe escribir el contenido del bloque");
               return false;
           }
        return true;

    }

    private void aceptar() {
        if(!taDescripcion.getText().isEmpty()){
            bloque=new Bloque();
            bloque.setDescripcion(taDescripcion.getText());
            BloqueFacade.getInstance().alta(bloque);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Debe escribir nombre del bloque");
            taDescripcion.requestFocus();
        }
    }

    private void inicializarComponentes() {
        Comunes.autoAjusteTextArea(taDescripcion);
    }

    private void inicializarComponentes() {
        cargarListaUnidadAcademica();
        jListUnidadAcademica.requestFocus();
    }

    private void cargarListaUnidadAcademica() {
        List<UnidadAcademica> listaUnidadAcademica = unidadAcademicaFacade.getTodasUnidadAcademica();
        DefaultListModel modeloLista = new DefaultListModel();
        for(UnidadAcademica unidadAcademica : listaUnidadAcademica) {
            modeloLista.addElement(unidadAcademica);
        }
        jListUnidadAcademica.setModel(modeloLista);
    }

    private void agregar() {
        diagUnidadAcademicaAlta unidadAcademicaAlta = new diagUnidadAcademicaAlta(null, true);
        unidadAcademicaAlta.setLocation(Comunes.centrarDialog(unidadAcademicaAlta));
        unidadAcademicaAlta.setVisible(true);
        inicializarComponentes();
    }

    private void modificar() {
        diagUnidadAcademicaModificacion unidadAcademicaModificacion = new diagUnidadAcademicaModificacion(null, true, (UnidadAcademica) jListUnidadAcademica.getSelectedValue());
        unidadAcademicaModificacion.setLocation(Comunes.centrarDialog(unidadAcademicaModificacion));
        unidadAcademicaModificacion.setVisible(true);
        inicializarComponentes();
    }


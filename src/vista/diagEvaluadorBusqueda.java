    private void buscarEvaluador(String text) {
       List<Evaluador> listaEval = EvaluadorFacade.getInstance().getEvaluadores(text);
        Comunes.cargarJList(jList1, listaEval);
    }

    private void escogerEvaluador() {
        if(jList1.getSelectedIndex() != -1) {
            evaluador = (Evaluador) jList1.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un evaluador de la lista",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public Evaluador getEvaluador() {
        return this.evaluador;
    }

    private void iniciarComponentes() {
        cargarEvaluadores();
       
    }

    private void cargarEvaluadores() {
        Comunes.cargarJList(jList1, EvaluadorFacade.getInstance().getTodosEvaluador());
    }

    private void agregarNuevoEvaluador() {
        diagNuevoEvaluador diNuevoEvaluador = new diagNuevoEvaluador(null, true, usuario);
        diNuevoEvaluador.setLocation(Comunes.centrarDialog(diNuevoEvaluador));
        diNuevoEvaluador.setVisible(true);
        if(diNuevoEvaluador.getInvestigador() != null){
            Evaluador evaluadorCreado = new Evaluador();
            evaluadorCreado.setInvestigador(diNuevoEvaluador.getInvestigador());
            EvaluadorFacade.getInstance().alta(evaluadorCreado);
        }
       cargarEvaluadores();
        
    }


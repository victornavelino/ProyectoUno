    private void modificarDatosPersonales() {
        if (jListInv1.getSelectedIndex() != -1) {
            Investigador investigador = (Investigador) jListInv1.getSelectedValue();
            Persona persona = investigador.getPersona();
            persona.setApellido(tfApellidos1.getText());
            persona.setNombre(tfNombres1.getText());
            try {
                DocumentoIdentidad documentoIdentidad = new DocumentoIdentidad();
                if ((!tfNumeroDocumento1.getText().equals("")) && (cboTipoDocumento1.getSelectedIndex() > 0)) {
                    documentoIdentidad.setNumero(Long.parseLong(tfNumeroDocumento1.getText()));
                    documentoIdentidad.setTipoDocumento((TipoDocumento) cboTipoDocumento1.getSelectedItem());
                } else {
                    documentoIdentidad = null;
                }
                persona.setDocumentoIdentidad(documentoIdentidad);
            } catch (Exception ex) {
            }
            if (!tfCuil1.getText().equals("")) {
                persona.setCuil(tfCuil1.getText());
            }
            if (jXDateFechaNacimiento1.getDate() != null) {
                persona.setFechaNacimiento(jXDateFechaNacimiento1.getDate());
            }
            if (cboSexo1.getSelectedIndex() > 0) {
                persona.setSexo((Sexo) cboSexo1.getSelectedItem());
            } else {
                persona.setSexo(null);
            }

            Domicilio domicilio = new Domicilio();
            domicilio.setCalle(tfCalle1.getText());
            domicilio.setNumero(tfNumero1.getText());
            domicilio.setPiso(tfPiso1.getText());
            domicilio.setDpto(tfDpto1.getText());
            domicilio.setEntreCalles(tfEntreCalles1.getText());
            domicilio.setReferencia(tfReferencia1.getText());
            domicilio.setBarrio(tfBarrio1.getText());
            domicilio.setLocalidad((Localidad) cboLocalidades1.getSelectedItem());
            domicilio.setCodigoPostal(tfCodigoPostal1.getText());
            persona.setDomicilio(domicilio);
            persona.setTelefonos(telefonos);
            persona.setCorreosElectronicos(correosElectronicos);
            investigador.setPersona(persona);
            InvestigadorFacade.getInstance().modificar(investigador);
        }
    }


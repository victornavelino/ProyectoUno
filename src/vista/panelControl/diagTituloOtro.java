 private void inicializarComponentes() {
        if (tipoOperacion.equals("Alta")) {
            tfDescripcion.setText("");
            tfDescripcion.requestFocus();
        } else if (tipoOperacion.equals("Consulta")) {
            tfDescripcion.setText(tituloOtro.toString());
            tfDescripcion.setEditable(false);
        } else if (tipoOperacion.equals("Modificación")) {
            tfDescripcion.setText(tituloOtro.toString());
        }

    }

    private void aceptar() {
        if (tipoOperacion.equals("Alta")) {
            tituloOtro = new TituloOtro();
            tituloOtro.setDescripcion(tfDescripcion.getText());
            TituloOtroFacade.getInstance().alta(tituloOtro);
        } else if (tipoOperacion.equals("Modificación")) {
            tituloOtro.setDescripcion(tfDescripcion.getText());
            TituloOtroFacade.getInstance().modificar(tituloOtro);
        }
        this.dispose();
    }

    public TituloOtro getTituloOtroCreada() {
        return tituloOtro;
    }



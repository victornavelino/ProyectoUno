    private void BuscarExpte() {
        if (!tfLetra.getText().isEmpty() && !tfNumero.getText().isEmpty()) {
            Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().buscarExp(tfLetra.getText(), tfNumero.getText()));
        }
        if (tfLetra.getText().isEmpty() && tfNumero.getText().isEmpty() && !tfAnio.getText().isEmpty()) {
            if (Comunes.validarInteger(tfAnio.getText())) {
                Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().buscarPorAño(Integer.parseInt(tfAnio.getText())));
            }

        }
        if (!tfLetra.getText().isEmpty() && tfNumero.getText().isEmpty()) {
            Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().busquedaPorLetra(tfLetra.getText()));
        }
        if (tfLetra.getText().isEmpty() && !tfNumero.getText().isEmpty() && tfAnio.getText().isEmpty()) {
            Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().busquedaPorNumero(tfNumero.getText()));
        }
        if (tfLetra.getText().isEmpty() && tfNumero.getText().isEmpty() && tfAnio.getText().isEmpty()) {
            Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().getExpedientes());
        }

    }

    private void BuscarExpteAvanzada() {
        if (cbIniciadorExtracto.getSelectedIndex() != -1) {
            if (tfIniExt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar un iniciador o parte del extracto", "Error", JOptionPane.ERROR_MESSAGE);
                tfIniExt.requestFocus();
            } else {
              if(cbIniciadorExtracto.getSelectedItem().equals("Iniciador")){
                Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().busquedaPorInciador(tfIniExt.getText()));
              }
              if(cbIniciadorExtracto.getSelectedItem().equals("Extracto")){
              Comunes.cargarJList(lstExpedientes, ExpedienteFacade.getInstance().busquedaPorEstracto(tfIniExt.getText()));
              }
                
            }

        }

    }

    private void inicializar() {
        lstExpediente = ExpedienteFacade.getInstance().getTodas();
        Comunes.cargarJList(lstExpedientes, lstExpediente);
    }

    private void seleccionarExpediente() {
        if(lstExpedientes.getSelectedIndex()!=-1){
          expediente = (Expediente)lstExpedientes.getSelectedValue();
          this.dispose();
        }
        else{
          JOptionPane.showMessageDialog(null, "No Seleccionó ningún Expediente de la Lista");
        }
    }


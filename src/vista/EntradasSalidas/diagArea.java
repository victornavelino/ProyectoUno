    private void inicializarComponentes() {
        Comunes.autoAjusteTextArea(taNombreArea);


        if (tipoOperacion.equals("modificación")) {
            if (area.getNombre() != null) {
                taNombreArea.setText(area.getNombre());
            } else {
                taNombreArea.setText("");
            }
        }
    }

    private void Aceptar() {
        if (tipoOperacion.equals("Alta")) {
            area = new Areas();
            if (!taNombreArea.getText().isEmpty()) {
                area.setNombre(taNombreArea.getText().toUpperCase());
            } else {
                area.setNombre(null);
            }
            if (!taNombreArea.getText().isEmpty()) {
                AreaFacade.getInstance().alta(area);
                JOptionPane.showMessageDialog(null, "Area Agregada");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "El Area debe tener un nombre!");
                taNombreArea.requestFocus();
            }


        } else if (tipoOperacion.equals("modificación")) {

            if (!taNombreArea.getText().isEmpty()) {
                area.setNombre(taNombreArea.getText().toUpperCase());
            } else {
                area.setNombre(null);
            }
            AreaFacade.getInstance().modificar(area);
            JOptionPane.showMessageDialog(null, "Area modificado");
            this.dispose();
        }
    }

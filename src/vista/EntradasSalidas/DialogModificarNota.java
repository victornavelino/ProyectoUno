    private void accionModificar() {
        nota.setNumero(tfNumero.getText());
        nota.setDescripcion(tfDescripcion.getText());
        try {
            NotaEntradaSalidaFacade.getInstance().modificar(nota);
            JOptionPane.showMessageDialog(null, "La Nota ha sido Modificada");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al tratar de modificar"+e);
        }
        this.dispose();
    }

 

    private void buscarProyecto(String text) {
        if (tfBusqueda.getText().trim().length() >= 3) {
            List<Proyecto> proyectosEncontrados = proyectoFacade.buscar(text);
            Comunes.cargarJList(jList1, proyectosEncontrados);
        }

    }

    private void escogerProyecto() {
        if (jList1.getSelectedIndex() != -1) {
            proyecto = (Proyecto) jList1.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un proyecto de la lista",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public Proyecto getProyecto() {
        return this.proyecto;
    }


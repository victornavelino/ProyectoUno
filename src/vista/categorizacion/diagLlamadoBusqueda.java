    private void inicializarComponentes() {
        this.setLocationRelativeTo(null);
        tfTexto.requestFocus();
        getRootPane().setDefaultButton(btnBuscar);
        habilitarBotones(false);
    }
    
    //MANEJO DE DATOS
    private void buscarLlamado() {
        List<Llamado> llamados = LlamadoFacade.getInstance().llamadoxDescripcion(tfTexto.getText());
        Comunes.cargarJList(lstDatos, llamados);
    }

    private void habilitarBotones(boolean valor) {        
        btnEliminar.setEnabled(valor);
        btnModificar.setEnabled(valor);        
    }

    private void modificarLlamado() {
        diagLlamadoModificacion llamadoModificacion = new diagLlamadoModificacion(null,true, null);
        llamadoModificacion.setVisible(true);
    }

    private void eliminarLlamado() {
        int eleccion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro "
                + "que desea eliminar El llamado "
                + lstDatos.getSelectedValue() + " ?",
                "Confirma Eliminacion?", JOptionPane.YES_NO_OPTION);
        if (eleccion == 0) {
            Llamado llamado =  (Llamado) lstDatos.getSelectedValue();
            facade.LlamadoFacade.getInstance().eliminacion(llamado);                      
            
            Operacion operacion = new Operacion();
            operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
            operacion.setOperacion("Baja de Llamado");
            operacion.setUsuario(usuario);
            OperacionFacade.getInstance().alta(operacion);
            buscarLlamado();
            JOptionPane.showMessageDialog(rootPane, "El llamado fue eliminado");                   
        }
    }

    private void agregarLlamado() {
        diagLlamadoAlta llamadoAlta = new diagLlamadoAlta(null, true, null);
        llamadoAlta.setVisible(true);
    }

    //MANEJO DE COMPONENTES
    private void inicializarComponentes() {
        this.setLocationRelativeTo(null);
        tfDescripcion.requestFocus();
        tfDescripcion.setText("");                
    }
    
    
    //MANEJO DE DATOS
    private boolean validar() {
        if(!tfDescripcion.getText().equals("")) {
            return true;
        } else {
            return false;
        }       
    }
    
    private void guardar() {
        if (validar()) {
            ComisionCategorizacion comision = new ComisionCategorizacion();
            comision.setDescripcion(tfDescripcion.getText());            
            ComisionCategorizacionFacade.getInstance().alta(comision);
            JOptionPane.showMessageDialog(null, "El Valor de la Comisi칩n \"" +
                    comision.getDescripcion() + "\" ha sido guardada "
                    + "exitosamente");
            Operacion operacion = new Operacion();
            operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
            operacion.setOperacion("Alta de Comisi칩n");
            operacion.setUsuario(usuario);
            OperacionFacade.getInstance().alta(operacion);
            inicializarComponentes();
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese una descripci칩n v치lida",
                    "Error", JOptionPane.ERROR_MESSAGE);
            inicializarComponentes();
        }
    }
    

    //MANEJO DE COMPONENTES
     private void inicializarComponentes() {
        this.setLocationRelativeTo(null);
        tfDescripcion.requestFocus();       
        tfDescripcion.setText("");
    }
     
    //MANEJO DE DATOS
    private boolean validar() {
        if(!tfDescripcion.getText().equals("")) {
            return true;
        } else {
            return false;
        }        
    }   

    private void guardar() {
        if (validar()) {            
            ValorCategoria valorCategoria = new ValorCategoria();            
            
            valorCategoria.setDescripcion(tfDescripcion.getText());
            ValorCategoriaFacade.getInstance().alta(valorCategoria);
            
            JOptionPane.showMessageDialog(null, "El Valor de Categoria \"" +
                    valorCategoria.getDescripcion() + "\" ha sido guardada "
                    + "exitosamente");
            Operacion operacion = new Operacion();
            operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
            operacion.setOperacion("Alta de Valor de Categoría");
            operacion.setUsuario(usuario);
            OperacionFacade.getInstance().alta(operacion);
            inicializarComponentes();
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese una descripción válida",
                    "Error", JOptionPane.ERROR_MESSAGE);
            inicializarComponentes();
        }
    }

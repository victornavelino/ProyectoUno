    private void cargarComboBoxGrupo() {
        Comunes.cargarJCombo(cboGrupo, GrupoFacade.getInstance().listarGruposExceptoAdministradores());
    }

    public void cargarDatosUsuario() {
        usuario.setNombreCompleto(tfNombreCompleto.getText());
        usuario.setNombreUsuario(tfNombreUsuario.getText());
        String password = new String(passContrasena.getPassword());
        String confirmaPassword = new String(passConfirmaContrasena.getPassword());
        if (!password.equals("")) {
            if (password.equals(confirmaPassword)) {
                try {
                    usuario.setContrasena(sha1.getHash(new String(passContrasena.getPassword())));
                } catch (NoSuchAlgorithmException ex) {
                    JOptionPane.showMessageDialog(this, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden, no se cambiará", "Error de Confirmación de Contraseña", JOptionPane.ERROR_MESSAGE);
                passConfirmaContrasena.setText("");
                passContrasena.setText("");
            }
        }
        usuario.setGrupo((Grupo) cboGrupo.getSelectedItem());
    }

    private void cargarTextFieldsUsuario() {
        tfNombreCompleto.requestFocus();
        tfNombreCompleto.setText(usuario.getNombreCompleto());
        tfNombreUsuario.setText(usuario.getNombreUsuario());
        cargarComboBoxGrupo();
        cboGrupo.setSelectedItem(usuario.getGrupo());
    }

    private void filtrarListado() {
        List<Usuario> listUsuario = usuarioFacade.listarUsuarios(tfBuscar.getText());
        DefaultListModel modeloLista = new DefaultListModel();
        for (int i = 0; i < listUsuario.size(); i++) {
            modeloLista.addElement(listUsuario.get(i));
        }
        jListClientes.setModel(modeloLista);
    }


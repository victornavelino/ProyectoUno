    private void inicializarComponentes() {
        usuarioesIncluidos = new UsuarioFacade().listarTodosUsuarios();
        usuarioesIncluidos.removeAll(usuarioesExcluidos);
        Comunes.cargarJList(jListMatriculasCatastrales, usuarioesIncluidos);
    }

    private void cargarJListTodosGruposInversores() {
        Comunes.cargarJList(jListMatriculasCatastrales, new UsuarioFacade().listarTodosUsuarios());
    }

    private void filtrar() {
        if (!tfFiltro.getText().equals("")) {
            usuarioesIncluidos = new UsuarioFacade().listarTodosUsuarios();
            usuarioesIncluidos.removeAll(usuarioesExcluidos);
            Comunes.cargarJList(jListMatriculasCatastrales, usuarioesIncluidos);
        } else {
            cargarJListTodosGruposInversores();
        }
    }

    private void escoger() {
        if (jListMatriculasCatastrales.getSelectedIndex() != -1) {
            usuarioEscogido = (Usuario) jListMatriculasCatastrales.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un grupo inversor",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public Usuario getUsuarioEscogido() {
        return usuarioEscogido;
    }


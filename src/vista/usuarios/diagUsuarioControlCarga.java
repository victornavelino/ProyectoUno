    private void inicializarComponentes() {
        cargarEncabezadosTablaOperaciones(modeloTablaOperaciones);
        configurarTabla(tblOperaciones);
        Comunes.cargarJList(jListUsuarios, new UsuarioFacade().listarTodosUsuarios());

    }

    private void cargarEncabezadosTablaOperaciones(ModeloTablaNoEditable modeloTabla) {
        modeloTabla.addColumn("Codigo");
        modeloTabla.addColumn("Usuario");
        modeloTabla.addColumn("Fecha");
        modeloTabla.addColumn("Operacion Realizada");
        tblOperaciones.setModel(modeloTabla);

    }

    private void configurarTabla(JTable tbl) {
        JViewport scroll = (JViewport) tbl.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tbl.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tbl.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (1 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (20 * ancho) / 100;
                    break;
                case 2:
                case 3:
                case 4:
                case 5:
                case 6:
                case 7:
                case 8:
                case 9:
                case 10:
                case 11:
                case 12:
                    anchoColumna = (5 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
            tbl.setColumnModel(modeloColumna);
        }
        tbl.getTableHeader().setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
        tbl.getTableHeader().setBackground(java.awt.Color.WHITE);
        tbl.getTableHeader().setForeground(Color.BLACK);
        //Si le queremos cambiar el tamaño a la tablita
        tbl.setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
    }

    private void cargarOperaciones() {


        modeloTablaOperaciones = new ModeloTablaNoEditable();
        cargarEncabezadosTablaOperaciones(modeloTablaOperaciones);
        if (usuario != null) {
            for (Operacion op : OperacionFacade.getInstance().listarOperacionesUsuario(usuario)) {
                cargarOperacion(op);

            }
            tblOperaciones.setModel(modeloTablaOperaciones);
        }

    }

    private void cargarOperacion(Operacion op) {
        Object[] fila = new Object[4];
        fila[0] = op.getId();
        fila[1] = op.getUsuario();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss");
        try {
            fila[2] = formato.format(op.getFecha());
        } catch (Exception ex) {
        }

        fila[3] = op.getOperacion();
        modeloTablaOperaciones.addRow(fila);
    }

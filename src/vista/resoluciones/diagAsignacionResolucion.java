    private void cargarResolucion() {
        diagResolucionEleccion diEleccion = new diagResolucionEleccion(null, true);
        diEleccion.setVisible(true);
        if (diEleccion.getResolucion() != null) {
            resolucion = diEleccion.getResolucion();
            tfResolucion.setText(resolucion.toString());
            evaluadores = resolucion.getEvaluadores();
            proyectos = resolucion.getProyectos();
            investigadores = resolucion.getInvestigadores();
            Comunes.cargarJList(jListProyectos, proyectos);
            Comunes.cargarJList(jListEvaluadores, evaluadores);
            Comunes.cargarJList(jListInvestigadores, investigadores);
        }
    }

    private boolean validarDatos() {
        if (resolucion != null) {
            return true;
        } else {
            return false;
        }
    }


    private void guardarResolucion() {
        if (validarDatos()) {
            try {
                resolucion.setEvaluadores(evaluadores);
                resolucion.setInvestigadores(investigadores);
                resolucion.setProyectos(proyectos);
                EntityManagerFactory emf = Persistence.createEntityManagerFactory("ProyectoUnoPU",ConexionFacade.PROPIEDADES);
                new ResolucionJpaController(emf).edit(resolucion);
                JOptionPane.showMessageDialog(null, "La asignación de la Resolución "
                        + "se ha realizado correctamente");
                this.dispose();
            } catch (NonexistentEntityException ex) {
                JOptionPane.showMessageDialog(null, "Error en la asignación de la Resolución "
                        + ex);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error en la asignación de la Resolución "
                        + ex);
            }


        }
    }

    private void agregarProyecto() {
        diagProyectoBusquedaSimple proyectoBusquedaSimple = new diagProyectoBusquedaSimple(null, true);
        Comunes.centrarDialog(proyectoBusquedaSimple);
        proyectoBusquedaSimple.setVisible(true);
        if (proyectoBusquedaSimple.getProyecto() != null) {
            proyectos.add(proyectoBusquedaSimple.getProyecto());
            Comunes.cargarJList(jListProyectos, proyectos);
        }
    }

    private void eliminarProyecto() {
        if (jListProyectos.getSelectedIndex() != -1) {
            proyectos.remove((Proyecto) jListProyectos.getSelectedValue());
            Comunes.cargarJList(jListProyectos, proyectos);
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un proyecto",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    private void inicializarComponentes() {
        cargarListaTituloPosgrado();
        jListTituloPosgrado.requestFocus();
    }

    private void cargarListaTituloPosgrado() {
        List<TituloPosgrado> listaTituloPosgrado = TituloPosgradoFacade.getInstance().getTodosTituloPosgrado();
        DefaultListModel modeloLista = new DefaultListModel();
        for(TituloPosgrado tituloPosgrado : listaTituloPosgrado) {
            modeloLista.addElement(tituloPosgrado);
        }
        jListTituloPosgrado.setModel(modeloLista);
    }

    private void agregar() {
        diagTituloPosgradoAlta tituloPosgradoAlta = new diagTituloPosgradoAlta(null, true);
        tituloPosgradoAlta.setLocation(Comunes.centrarDialog(tituloPosgradoAlta));
        tituloPosgradoAlta.setVisible(true);
        inicializarComponentes();
    }

    private void modificar() {
        diagTituloPosgradoModificacion tituloPosgradoModificacion = new diagTituloPosgradoModificacion(null, true, (TituloPosgrado) jListTituloPosgrado.getSelectedValue());
        tituloPosgradoModificacion.setLocation(Comunes.centrarDialog(tituloPosgradoModificacion));
        tituloPosgradoModificacion.setVisible(true);
        inicializarComponentes();
    }


    private void cargarJListUnidadAcademica() {
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        List<UnidadAcademica> unidadesAcademicas = UnidadAcademicaFacade.getInstance().getTodasUnidadAcademica();
        for(UnidadAcademica unidadAcademica : unidadesAcademicas) {
            modeloCombo.addElement(unidadAcademica);
        }
        cboUnidadAcademica.setModel(modeloCombo);
    }

    private void cargarJListTipoProyecto() {
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        List<TipoProyecto> tiposProyectos = TipoProyectoFacade.getInstance().getTodosTipoProyecto();
        for(TipoProyecto tipoProyecto : tiposProyectos) {
            modeloCombo.addElement(tipoProyecto);
        }
        cboTipoProyecto.setModel(modeloCombo);
    }

    


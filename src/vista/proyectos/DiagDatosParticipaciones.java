    private void inicializarComponentes() {
        jXDatePicker1.setDate(null);
        jXDatePicker2.setDate(null);
        jTextField1.setText("");
        Comunes.cargarJCombo(cboParticipaciones, proyectoFacade.getParticipaciones(investigador, proyecto));
        jLabel5.setText("Investigador: " + investigador);
        jLabel6.setText("Proyecto: " + proyecto);
        Comunes.cargarJCombo(jComboBox2, new RolFacade().getTodos());
        cargarDatosParticipacionSeleccionada();
    }

    private void eliminarParticipacionSeleccionada() {
        if (cboParticipaciones.getSelectedIndex() != -1) {
            proyectoFacade.eliminarParticipacion((Participacion) cboParticipaciones.getSelectedItem());
            inicializarComponentes();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger una participación",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void agregarNuevaParticipacion() {
        diagDatosNuevosInvestigador nuevaParticipacion =
                new diagDatosNuevosInvestigador(null, true, investigador, proyecto, usuario);
        nuevaParticipacion.setLocation(Comunes.centrarDialog(nuevaParticipacion));
        nuevaParticipacion.setVisible(true);
        inicializarComponentes();
    }

    private void cargarDatosParticipacionSeleccionada() {
        if (cboParticipaciones.getSelectedIndex() != -1) {
            Participacion participacionSeleccionada = (Participacion) cboParticipaciones.getSelectedItem();
            jXDatePicker1.setDate(participacionSeleccionada.getFechaDesde());
            jXDatePicker2.setDate(participacionSeleccionada.getFechaHasta());
            jComboBox2.setSelectedItem(participacionSeleccionada.getRol());
            jTextField1.setText(String.valueOf(participacionSeleccionada.getDedicacionSemanal()));
            jTfResolucionAlta.setText(participacionSeleccionada.getResolucionAlta());
            jTfResolucionBaja.setText(participacionSeleccionada.getResolucionBaja());
        } else {
        }
    }

    private void modificarParticipacionSeleccionada() {
        if (jXDatePicker2.getDate().after(jXDatePicker1.getDate())) {
            if (IntegerValidator.getInstance().isValid(jTextField1.getText())) {
                Participacion participacionSeleccionada = (Participacion) cboParticipaciones.getSelectedItem();
                participacionSeleccionada.setFechaDesde(jXDatePicker1.getDate());
                participacionSeleccionada.setFechaHasta(jXDatePicker2.getDate());
                participacionSeleccionada.setRol((Rol) jComboBox2.getSelectedItem());
                participacionSeleccionada.setDedicacionSemanal(Integer.parseInt(jTextField1.getText()));
                
                String resolucionAlta;
                        String resolucionBaja;
                        if(jTfResolucionAlta.getText()!=null){
                            resolucionAlta=jTfResolucionAlta.getText();
                        }else{
                            resolucionAlta=null;
                        }
                        if(jTfResolucionBaja.getText()!=null){
                            resolucionBaja=jTfResolucionBaja.getText();
                        }else{
                            resolucionBaja=null;
                        }
                participacionSeleccionada.setResolucionAlta(resolucionAlta);
                participacionSeleccionada.setResolucionBaja(resolucionBaja);

                new ParticipacionFacade().modificar(participacionSeleccionada);

                Operacion operacion = new Operacion();
                operacion.setFecha(Comunes.obtenerFechaActualDesdeDB());
                operacion.setOperacion("Agregación de Integrante en Proyecto");
                operacion.setUsuario(usuario);
                OperacionFacade.getInstance().alta(operacion);

                inicializarComponentes();
                cargarDatosParticipacionSeleccionada();
                JOptionPane.showMessageDialog(rootPane, "Modificación realizada");
            } else {
                JOptionPane.showMessageDialog(null, "Dedicación semanal incorrecta",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Fechas incorrectas. La fecha "
                    + "desde debe ser anterior a la fecha hasta",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

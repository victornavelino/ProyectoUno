    private void inicializarComponentes() {
        tituloGradoesIncluidos = TituloGradoFacade.getInstance().getTodosTituloGrado();
        tituloGradoesIncluidos.removeAll(tituloGradoesExcluidos);
        Comunes.cargarJList(jListMatriculasCatastrales, tituloGradoesIncluidos);
    }

    private void cargarJListTodosGruposInversores() {
        Comunes.cargarJList(jListMatriculasCatastrales, TituloGradoFacade.getInstance().getTodosTituloGrado());
    }

    private void filtrar() {
        if (!tfFiltro.getText().equals("")) {
            Comunes.cargarJList(jListMatriculasCatastrales, TituloGradoFacade.getInstance().filtrar(tfFiltro.getText()));
           
        } else {
            cargarJListTodosGruposInversores();
        }
    }

    private void escoger() {
        if (jListMatriculasCatastrales.getSelectedIndex() != -1) {
            tituloGradoEscogido = (TituloGrado) jListMatriculasCatastrales.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un grupo inversor",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public TituloGrado getTituloGradoEscogido() {
        return tituloGradoEscogido;
    }

    private void agregarNueva() {
        diagTituloGrado tituloGradoAlta = new diagTituloGrado(null, true, "Alta");
        tituloGradoAlta.setLocation(Comunes.centrarDialog(tituloGradoAlta));
        tituloGradoAlta.setVisible(true);
        if(tituloGradoAlta.getTituloGradoCreada() != null) {
            tituloGradoEscogido = tituloGradoAlta.getTituloGradoCreada();
            this.dispose();
        }
    }



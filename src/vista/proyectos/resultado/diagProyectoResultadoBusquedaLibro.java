    private void inicializarcomponentes() {
        
        Comunes.cargarJList(jListBusquedaLibros,PublicacionFacade.getInstance().getLibros());
    }

    

    private void buscarLibro(String text) {
        List<Libro> librosEncontrados = PublicacionFacade.getInstance().getLibros(text);
        Comunes.cargarJList(jListBusquedaLibros, librosEncontrados);
        
    }

    private void escogerLibro() {
        if(jListBusquedaLibros.getSelectedIndex() != -1) {
            libro = (Libro) jListBusquedaLibros.getSelectedValue();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un libro de la lista",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    public Libro getLibro() {
        return libro;
    }
    

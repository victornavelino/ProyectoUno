    private void agregarIntegrantesLibro() {
        if (lsIntegrantesFueraLibro.getSelectedIndex() != -1) {

            integrantesDentroLibro.add((Investigador) lsIntegrantesFueraLibro.getSelectedValue());
            List<Investigador> integrantesFuera = InvestigadorFacade.getInstance().getTodosInvestigador();
            integrantesFuera.removeAll(integrantesDentroLibro);
            Comunes.cargarJList(lsIntegrantesFueraLibro, integrantesFuera);

            Comunes.cargarJList(lstIntegrantesDentroLibro, integrantesDentroLibro);

        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe escoger un Autor", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void quitarIntegrantesLibro() {
        if (lstIntegrantesDentroLibro.getSelectedIndex() != -1) {

            integrantesDentroLibro.remove((Investigador) lstIntegrantesDentroLibro.getSelectedValue());
            List<Investigador> integrantesFuera = InvestigadorFacade.getInstance().getTodosInvestigador();
            integrantesFuera.removeAll(integrantesDentroLibro);
            Comunes.cargarJList(lsIntegrantesFueraLibro, integrantesFuera);

            Comunes.cargarJList(lstIntegrantesDentroLibro, integrantesDentroLibro);

        }
    }

    private void buscarIntegrantesLibro() {
        if (!tfBuscarIntregantesLibro.getText().equals("")) {
            Comunes.cargarJList(lsIntegrantesFueraLibro, InvestigadorFacade.getInstance().buscarPorApellidoNombre(tfBuscarIntregantesLibro.getText()));
        } else {
            Comunes.cargarJList(lsIntegrantesFueraLibro, InvestigadorFacade.getInstance().getTodosInvestigador());
        }
    }

    private void inicializarComponentes() {
        Comunes.formatearJXdatePicker(jXDatePickerEnviadoLibro);
        Comunes.formatearJXdatePicker(jXDatePickerAcaptadoLibro);
        Comunes.formatearJXdatePicker(jXDatePickerPublicadoLibro);
        cboEdicionLibro.setModel(new javax.swing.DefaultComboBoxModel(TipoEdicion.values()));
        Comunes.autoAjusteTextArea(taProyectoLibro);
        if (tipoOperacion.equals("Alta")) {
            if (proyectoVinculacionLibro != null) {
                cargarIntegrantesProyectoVinculacion();
            } else {

                CargarIntegrandesProyecto();
            }
        }
        if (tipoOperacion.equals("Modificación")) {

            Comunes.cargarJList(lstIntegrantesDentroLibro, libro.getInvestigadores());
            integrantesDentroLibro = libro.getInvestigadores();
            List<Investigador> integrantesFuera = InvestigadorFacade.getInstance().getTodosInvestigador();
            integrantesFuera.removeAll(libro.getInvestigadores());
            Comunes.cargarJList(lsIntegrantesFueraLibro, integrantesFuera);
            if (libro.getTitulo() != null) {
                tfTituloLibro.setText(libro.getTitulo());
            } else {
                tfTituloLibro.setText(" ");
            }
            if (libro.getEditor() != null) {
                tfEditorLibro.setText(libro.getEditor());
            } else {
                tfEditorLibro.setText(" ");
            }
            try {
                jXDatePickerAcaptadoLibro.setDate(libro.getFechaAceptado());
            } catch (Exception ex) {
            }
            try {
                jXDatePickerEnviadoLibro.setDate(libro.getFechaEnviado());
            } catch (Exception ex) {
            }
            try {
                jXDatePickerPublicadoLibro.setDate(libro.getFechaPublicado());
            } catch (Exception ex) {
            }

            cboEdicionLibro.setSelectedItem(libro.getTipoEdicion());
            if (libro.getISBN() != null) {
                tfISBNLibro.setText(libro.getISBN());
            } else {
                tfISBNLibro.setText(" ");
            }
            if (libro.getAnioPublicacion() != null) {
                tfAnioLibro.setText(libro.getAnioPublicacion());
            } else {
                tfAnioLibro.setText(" ");
            }
            if (libro.getLugarPublicacion() != null) {
                tfLugarLibro.setText(libro.getLugarPublicacion());
            } else {
                tfLugarLibro.setText(" ");
            }

        }
    }

    private void seleccionarProyecto() {
        diagProyectoBusquedaSimple selecproyecto = new diagProyectoBusquedaSimple(null, true);
        selecproyecto.setLocation(Comunes.centrarDialog(selecproyecto));
        selecproyecto.setVisible(true);
        if (selecproyecto.getProyecto() != null) {
            proyectoLibro = selecproyecto.getProyecto();
            taProyectoLibro.setText(proyectoLibro.toString());
            integrantesDentroLibro = new ArrayList<Investigador>();
            List<Participacion> participantes = selecproyecto.getProyecto().getParticipaciones();
            for (Participacion par : participantes) {
                if (par.getRol().getDescripcion().equals("Director")) {
                    integrantesDentroLibro.add(par.getInvestigador());
                }
            }
            Comunes.cargarJList(lstIntegrantesDentroLibro, integrantesDentroLibro);

        }
    }

    private void agregarLibro() {
        if (validar()) {
            if (tipoOperacion.equals("Alta")) {

                libro = new Libro();
                if (!tfTituloLibro.getText().isEmpty()) {
                    libro.setTitulo(tfTituloLibro.getText());
                } else {
                    libro.setTitulo(null);
                }
                if (proyectoLibro != null) {
                    List<Proyecto> proyectos = new ArrayList<Proyecto>();
                    proyectos.add(proyectoLibro);
                    libro.setProyectos(proyectos);


                }

                if (proyectoVinculacionLibro != null) {
                    List<ProyectoVinculacion> proyectosVinculacion = new ArrayList<ProyectoVinculacion>();
                    proyectosVinculacion.add(proyectoVinculacionLibro);
                    libro.setProyectosVinculacion(proyectosVinculacion);
                }

                libro.setTipoEdicion((TipoEdicion) cboEdicionLibro.getSelectedItem());

                if (!tfEditorLibro.getText().isEmpty()) {
                    libro.setEditor(tfEditorLibro.getText());
                } else {
                    libro.setEditor(null);
                }
                if (integrantesDentroLibro.size() > 0) {
                    libro.setInvestigadores(integrantesDentroLibro);
                } else {
                    libro.setInvestigadores(null);
                }
                if (!tfAnioLibro.getText().isEmpty()) {
                    libro.setAnioPublicacion(tfAnioLibro.getText());
                } else {
                    libro.setAnioPublicacion(null);
                }
                if (!tfISBNLibro.getText().isEmpty()) {
                    libro.setISBN(tfISBNLibro.getText());
                } else {
                    libro.setISBN(null);
                }
                if (!tfLugarLibro.getText().isEmpty()) {
                    libro.setLugarPublicacion(tfLugarLibro.getText());
                } else {
                    libro.setLugarPublicacion(null);
                }

                libro.setFechaAceptado(jXDatePickerAcaptadoLibro.getDate());
                libro.setFechaEnviado(jXDatePickerEnviadoLibro.getDate());
                libro.setFechaPublicado(jXDatePickerPublicadoLibro.getDate());

                //crear libro
                PublicacionFacade.getInstance().alta(libro);
                JOptionPane.showMessageDialog(null, "Libro guardado correctamente");
                this.dispose();
            }
            if (tipoOperacion.equals("Modificación")) {


                if (!tfTituloLibro.getText().isEmpty()) {
                    libro.setTitulo(tfTituloLibro.getText());
                } else {
                    libro.setTitulo(null);
                }
                if (proyectoLibro != null) {

                    List<Proyecto> proyectos = new ArrayList<Proyecto>();
                    proyectos.add(proyectoLibro);
                    libro.setProyectos(proyectos);
                }
                if (proyectoVinculacionLibro != null) {
                    List<ProyectoVinculacion> proyectosVinculacion = new ArrayList<ProyectoVinculacion>();
                    proyectosVinculacion.add(proyectoVinculacionLibro);
                    libro.setProyectosVinculacion(proyectosVinculacion);
                }
                libro.setTipoEdicion((TipoEdicion) cboEdicionLibro.getSelectedItem());

                if (!tfEditorLibro.getText().isEmpty()) {
                    libro.setEditor(tfEditorLibro.getText());
                } else {
                    libro.setEditor(null);
                }
                if (integrantesDentroLibro.size() > 0) {
                    libro.setInvestigadores(integrantesDentroLibro);
                } else {
                    libro.setInvestigadores(null);
                }
                if (!tfAnioLibro.getText().isEmpty()) {
                    libro.setAnioPublicacion(tfAnioLibro.getText());
                } else {
                    libro.setAnioPublicacion(null);
                }
                if (!tfISBNLibro.getText().isEmpty()) {
                    libro.setISBN(tfISBNLibro.getText());
                } else {
                    libro.setISBN(null);
                }
                if (!tfLugarLibro.getText().isEmpty()) {
                    libro.setLugarPublicacion(tfLugarLibro.getText());
                } else {
                    libro.setLugarPublicacion(null);
                }

                libro.setFechaAceptado(jXDatePickerAcaptadoLibro.getDate());
                libro.setFechaEnviado(jXDatePickerEnviadoLibro.getDate());
                libro.setFechaPublicado(jXDatePickerPublicadoLibro.getDate());

                //Modificar libro
                PublicacionFacade.getInstance().modificar(libro);


                JOptionPane.showMessageDialog(null, "Libro modificado correctamente");
                this.dispose();
            }
        }
    }

    private boolean validar() {
        boolean flag = false;
        if (proyectoLibro != null || proyectoVinculacionLibro != null) {
            if (!tfTituloLibro.getText().isEmpty()) {
                if (!integrantesDentroLibro.isEmpty()) {
                    if (tfAnioLibro.getText().isEmpty() || Comunes.validarInteger(tfAnioLibro.getText())) {
                        flag = true;
                    } else {
                        JOptionPane.showMessageDialog(null, "Debe escribir el año solo con numeros");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Debe seleccionar al menos un autor");
                }

            } else {
                JOptionPane.showMessageDialog(null, "Debe Ingresar un Título");
                tfTituloLibro.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un proyecto");
        }

        return flag;
    }


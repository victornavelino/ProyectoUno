    private void cargarIntegrantesProyectoVinculacion() {
        integrantesDentroLibro = ProyectoVinculacionFacade.getInstance().getTodosIntegrantes(proyectoVinculacionLibro);
        Comunes.cargarJList(lstIntegrantesDentroLibro, integrantesDentroLibro);
        List<Investigador> integrantesFuera = InvestigadorFacade.getInstance().getTodosInvestigador();
        integrantesFuera.removeAll(integrantesDentroLibro);
        Comunes.cargarJList(lsIntegrantesFueraLibro, integrantesFuera);
    }

    private void CargarIntegrandesProyecto() {
        integrantesDentroLibro = ProyectoFacade.getInstance().getTodosIntegrantes(proyectoLibro);
        Comunes.cargarJList(lstIntegrantesDentroLibro, integrantesDentroLibro);
        List<Investigador> integrantesFuera = InvestigadorFacade.getInstance().getTodosInvestigador();
        integrantesFuera.removeAll(integrantesDentroLibro);
        Comunes.cargarJList(lsIntegrantesFueraLibro, integrantesFuera);
    }

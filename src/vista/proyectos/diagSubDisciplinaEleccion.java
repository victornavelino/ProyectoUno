    private void inicializarComponentes() {
        cargarAreasTematicas();
    }

    private void cargarAreasTematicas() {
        Comunes.cargarJCombo(cboAreaTematica, AreaTematicaFacade.getInstance().getTodosAreaTematica());
        if (cboAreaTematica.getSelectedIndex() != -1) {
            cargarDisciplinasCientificas((AreaTematica) cboAreaTematica.getSelectedItem());
        }
    }

    private void cargarDisciplinasCientificas(AreaTematica areaTematica) {
        Comunes.cargarJCombo(cboDisciplinaCientifica, areaTematica.getDisciplinasCientificas());
        if(cboDisciplinaCientifica.getSelectedIndex() != -1) {
            cargarSubDisciplinasCientificas((DisciplinaCientifica) cboDisciplinaCientifica.getSelectedItem());
        }
    }

    private void cargarSubDisciplinasCientificas(DisciplinaCientifica disciplinaCientifica) {
        Comunes.cargarJCombo(cboSubDisciplinaCientifica, disciplinaCientifica.getSubDisciplinasCientificas());
        cargarSubDisciplinaCientificaSeleccionada();
    }

    public SubDisciplinaCientifica getSubDisciplinaCientifica() {
        return subDisciplinaCientifica;
    }

    private void cargarSubDisciplinaCientificaSeleccionada() {
        if (cboSubDisciplinaCientifica.getSelectedIndex() != -1) {
            subDisciplinaCientifica = (SubDisciplinaCientifica) cboSubDisciplinaCientifica.getSelectedItem();
        }
    }


    private void aceptar() {
        if (validar()) {
            recursoCreado = new RecursoHumanoAdquirir();
            recursoCreado.setEspecialidad(jtfEspecialidad.getText());
            recursoCreado.setFuncion(jtfFuncion.getText());
            recursoCreado.setMesesParticipacion(Integer.parseInt(jtfParticipacion.getText()));
            if (validar2()) {

                this.dispose();
            }
        }
    }

    public RecursoHumanoAdquirir getRecursoCreado() {
        return recursoCreado;
    }

    public boolean validar2() {
        boolean flag = false;
        try {
            recursoCreado.setDedicacion(Float.valueOf(jtfDedicacion.getText()));
            recursoCreado.setCostoTotalMensual(Float.valueOf(jtfCostoTotalMensual.getText()));
            recursoCreado.setCostoTotal(Float.valueOf(jtfCostoTotal.getText()));
            recursoCreado.setFinanciaM(Float.valueOf(jtfFinanciaM.getText()));
            recursoCreado.setFinanciaC(Float.valueOf(jtfFinanciaC.getText()));
            flag = true;
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return flag;
    }

    public boolean validar() {
        boolean flag = false;
        if (!jtfEspecialidad.getText().isEmpty()) {
            if (!jtfFuncion.getText().isEmpty()) {



                if (!jtfCostoTotalMensual.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfCostoTotalMensual.getText())) {
                    if (!jtfDedicacion.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfDedicacion.getText())) {
                        if (!jtfParticipacion.getText().isEmpty()) {// && IntegerValidator.getInstance().isValid(jtfParticipacion.getText())) {


                            if (!jtfCostoTotal.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfCostoTotal.getText())) {
                                if (!jtfFinanciaM.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfFinanciaM.getText())) {
                                    if (!jtfFinanciaC.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfFinanciaC.getText())) {
                                        try {

                                            if (Float.valueOf(jtfCostoTotal.getText())
                                                    == Float.valueOf(jtfFinanciaM.getText())
                                                    + Float.valueOf(jtfFinanciaC.getText())) {
                                                flag = true;
                                            } else {
                                                JOptionPane.showMessageDialog(null, "La suma de las financiaciones "
                                                        + "es distinta al total.",
                                                        "Error", JOptionPane.ERROR_MESSAGE);
                                            }
                                        } catch (NumberFormatException e) {
                                            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);

                                        }

                                    } else {
                                        JOptionPane.showMessageDialog(null, "Valor de financiación contraparte incorrecto",
                                                "Error", JOptionPane.ERROR_MESSAGE);
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(null, "Valor de financiación MINCyT incorrecto",
                                            "Error", JOptionPane.ERROR_MESSAGE);
                                }
                            } else {
                                JOptionPane.showMessageDialog(null, "Valor total incorrecto",
                                        "Error", JOptionPane.ERROR_MESSAGE);
                            }


                        } else {
                            JOptionPane.showMessageDialog(null, "Valor de meses de participación incorrecto",
                                    "Error", JOptionPane.ERROR_MESSAGE);
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Valor de dedicación incorrecto",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Costo total mensual incorrecto",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Función en el proyecto incorrecto",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Especialidad incorrecta",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        return flag;

    }

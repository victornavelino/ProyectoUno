    private void cargarDatos() {
        etapaCreada.setNroEtapa(Integer.parseInt(jtfNroEtapa.getText()));
        etapaCreada.setFechaInicio(jxFechaInicio.getDate());
        etapaCreada.setFechaFin(jxFechaFin.getDate());
        etapaCreada.setObservaciones(taObservaciones.getText());
        try {
            etapaCreada.setBienCapitalFM(Float.valueOf(jtfbiendecapfm.getText()));
            etapaCreada.setConsultoriasFM(Float.valueOf(jtfconsulfm.getText()));
            etapaCreada.setMaterialesFM(Float.valueOf(jtfmatfm.getText()));
            etapaCreada.setOtrosRecursosFM(Float.valueOf(jtfotrosfm.getText()));
            etapaCreada.setRecHumFM(Float.valueOf(jtfrechfm.getText()));
            etapaCreada.setBienCapitalFC(Float.valueOf(jtfbcfc.getText()));
            etapaCreada.setConsultoriasFC(Float.valueOf(jtfconsulfc.getText()));
            etapaCreada.setMaterialesFC(Float.valueOf(jtfmatfc.getText()));
            etapaCreada.setOtrosRecursosFC(Float.valueOf(jtfotrofc.getText()));
            etapaCreada.setRecHumFC(Float.valueOf(jtfrechfc.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);

        }
    }


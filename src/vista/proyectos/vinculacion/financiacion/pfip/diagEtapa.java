    private void inicializarComponentes() {
        jxFechaInicio.setFormats(new SimpleDateFormat("dd/MM/yyyy"));
        jxFechaFin.setFormats(new SimpleDateFormat("dd/MM/yyyy"));
        switch (operacion) {
            case "Alta":
                setearTextField();


                break;
            case "Modificacion":
                jxFechaInicio.setDate(etapaCreada.getFechaInicio());
               jxFechaFin.setDate(etapaCreada.getFechaFin());
                jtfNroEtapa.setText(etapaCreada.getNroEtapa().toString());
                taObservaciones.setText(etapaCreada.getObservaciones());
                
                if (etapaCreada.getBienCapitalFC() != null) {
                    jtfbcfc.setText(etapaCreada.getBienCapitalFC().toString());
                } else {
                    jtfbcfc.setText("0.0");
                }
                if (etapaCreada.getBienCapitalFM() != null) {
                    jtfbiendecapfm.setText(etapaCreada.getBienCapitalFM().toString());
                } else {
                    jtfbiendecapfm.setText("0.0");
                }
                if (etapaCreada.getConsultoriasFC() != null) {
                    jtfconsulfc.setText(etapaCreada.getConsultoriasFC().toString());
                } else {
                    jtfconsulfc.setText("0.0");
                }

                if (etapaCreada.getConsultoriasFM() != null) {
                    jtfconsulfm.setText(etapaCreada.getConsultoriasFM().toString());
                } else {
                    jtfconsulfm.setText("0.0");
                }

                if (etapaCreada.getMaterialesFC() != null) {
                    jtfmatfc.setText(etapaCreada.getMaterialesFC().toString());
                } else {
                    jtfmatfc.setText("0.0");
                }

                if (etapaCreada.getMaterialesFM() != null) {
                    jtfmatfm.setText(etapaCreada.getMaterialesFM().toString());
                } else {
                    jtfmatfm.setText("0.0");
                }

                if (etapaCreada.getOtrosRecursosFC() != null) {
                    jtfotrofc.setText(etapaCreada.getOtrosRecursosFC().toString());
                } else {
                    jtfotrofc.setText("0.0");
                }

                if (etapaCreada.getOtrosRecursosFM() != null) {
                    jtfotrosfm.setText(etapaCreada.getOtrosRecursosFM().toString());
                } else {
                    jtfotrosfm.setText("0.0");
                }

                if (etapaCreada.getRecHumFC() != null) {
                    jtfrechfc.setText(etapaCreada.getRecHumFC().toString());
                } else {
                    jtfrechfc.setText("0.0");
                }

                if (etapaCreada.getRecHumFM() != null) {
                    jtfrechfm.setText(etapaCreada.getRecHumFM().toString());
                } else {
                    jtfrechfm.setText("0.0");
                }
                cambiarTotalContraparte();
                cambiarTotalMincyt();
                break;
        }
    }


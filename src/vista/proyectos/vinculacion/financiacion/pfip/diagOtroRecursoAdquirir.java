    private void aceptar() {
        if (validar()) {
            otroRecursoAdquirir = new OtroRecursoAdquirir();
            otroRecursoAdquirir.setDescripcion(jtfDescripcion.getText());
            if (validar2()) {

                this.dispose();
            }
        }
    }

    public boolean validar2() {
        boolean flag = false;
        try {
            otroRecursoAdquirir.setPrecioTotal(Float.valueOf(jtfValor.getText()));
            otroRecursoAdquirir.setFinanciaC(Float.valueOf(jtfValorContraparte.getText()));

            otroRecursoAdquirir.setFinanciaM(Float.valueOf(jtfValorMincyt.getText()));
            flag = true;
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return flag;
    }

    public OtroRecursoAdquirir getRecursoCreado() {
        return otroRecursoAdquirir;
    }

    public boolean validar() {
        boolean flag = false;
        if (!jtfDescripcion.getText().isEmpty()) {


            if (!jtfValor.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfValor.getText())) {
                if (!jtfValorMincyt.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfValorMincyt.getText())) {
                    if (!jtfValorContraparte.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfValorContraparte.getText())) {
                        try {
                            if (Float.valueOf(jtfValor.getText())
                                    == Float.valueOf(jtfValorMincyt.getText())
                                    + Float.valueOf(jtfValorContraparte.getText())) {
                                flag = true;
                            } else {
                                JOptionPane.showMessageDialog(null, "La suma de las financiaciones "
                                        + "es distinta al total.",
                                        "Error", JOptionPane.ERROR_MESSAGE);
                            }
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);

                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Valor de financiación contraparte incorrecto",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Valor de financiación MINCyT incorrecto",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Valor total incorrecto",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }


        } else {
            JOptionPane.showMessageDialog(null, "Descripción incorrecta",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        return flag;

    }

    private void aceptar() throws ParseException {
        if (validar()) {
            bienDeCapital = new BienDeCapital();
            bienDeCapital.setDescripcion(jtfDescripcion.getText());
            if (validar2()) {
                this.dispose();

            }
        }
    }

    private boolean validar2() {
        boolean flag = false;
        try {
            bienDeCapital.setValor(Float.valueOf(jtfValor.getText()));
            flag = true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return flag;

    }

    private boolean validar() {

        boolean flag = false;
        if (!jtfDescripcion.getText().isEmpty()) {
            if (!jtfValor.getText().isEmpty()) {

                //&& FloatValidator.getInstance().isValid(jtfValor.getText())) {

                flag = true;


            } else {
                JOptionPane.showMessageDialog(null, "Valor incorrecto",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Descripción incorrecta",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        return flag;


    }


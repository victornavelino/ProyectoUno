    private void aceptar() {
        if (validar()) {
            material = new Material();
            material.setDetalle(jtfDetalle.getText());
            material.setCantidad(Integer.parseInt(jtfCantidad.getText()));
            material.setCostoTotal(calcularTotal());
            if (validar2()) {

                this.dispose();
            }
        }
    }

    public boolean validar2() {
        boolean flag = false;
        try {
            material.setCostoUnitario(Float.valueOf(jtfCostoUnitario.getText()));
            material.setFinanciaM(Float.valueOf(jtfParteM.getText()));
            material.setFinanciaC(Float.valueOf(jtfParteC.getText()));
            flag = true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);

        }
        return flag;
    }

    public Material getMaterialCreado() {
        return material;
    }

    public boolean validar() {
        boolean flag = false;
        if (!jtfDetalle.getText().isEmpty()) {
            if (!jtfCantidad.getText().isEmpty() && IntegerValidator.getInstance().isValid(jtfCantidad.getText())) {
                if (!jtfCostoUnitario.getText().isEmpty()) { //&& FloatValidator.getInstance().isValid(jtfCostoUnitario.getText())) {
                    if (!jtfCostoTotal.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfCostoTotal.getText())) {
                        if (!jtfParteM.getText().isEmpty()) {// && FloatValidator.getInstance().isValid(jtfParteM.getText())) {
                            if (!jtfParteC.getText().isEmpty()) {
                                try {
                                    // && FloatValidator.getInstance().isValid(jtfParteC.getText())) {


                                    if (Float.valueOf(jtfCostoTotal.getText())
                                            == Float.valueOf(jtfParteC.getText())
                                            + Float.valueOf(jtfParteM.getText())) {
                                        flag = true;

                                    } else {
                                        JOptionPane.showMessageDialog(null, "La suma de las financiaciones "
                                                + "es distinta al total.",
                                                "Error", JOptionPane.ERROR_MESSAGE);
                                    }
                                } catch (NumberFormatException ex) {
                                    JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);
                                }



                            } else {
                                JOptionPane.showMessageDialog(null, "Valor de financiación contraparte incorrecto",
                                        "Error", JOptionPane.ERROR_MESSAGE);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Valor de financiación MINCyT incorrecto",
                                    "Error", JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Valor total incorrecto",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Valor unitario incorrecto",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                JOptionPane.showMessageDialog(null, "Cantidad incorrecta",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Descripción incorrecta",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        return flag;
    }

    private void cambiarTotal() {
        jtfCostoTotal.setText(calcularTotal().toString());    }

    private Float calcularTotal() {
        float total = 0;
        try {
            total = Float.valueOf(jtfCantidad.getText()) * Float.valueOf(jtfCostoUnitario.getText());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Numeros mal formados", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return total;

    }

    private void setearTextField() {
        jtfCantidad.setText("0");
//        jtfCostoTotal.setText("0.0");
        jtfCostoUnitario.setText("0.0");
    }

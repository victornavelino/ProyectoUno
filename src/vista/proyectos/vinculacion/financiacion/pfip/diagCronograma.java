    private void cargarNroEtapa() {
        if (cronogramas.isEmpty()) {
            jtfEtapaCrono.setText("1");
        } else {
            jtfEtapaCrono.setText(Integer.toString(cronogramas.size() + 1));

        }

    }

    public boolean validar() {
        boolean flag = false;
        if (!jtfDesembolsos.getText().isEmpty()) {
            //&& FloatValidator.getInstance().isValid(jtfDesembolsos.getText())) {
            if (cboetapa.getSelectedItem() != null) {
                if (!jtfCosto.getText().isEmpty()
                        && FloatValidator.getInstance().isValid(jtfCosto.getText())) {
                    if (!jtfSaldo.getText().isEmpty()
                            && FloatValidator.getInstance().isValid(jtfSaldo.getText())) {
                        flag = true;
                    } else {
                        JOptionPane.showMessageDialog(null, "Saldo incorrecto",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Costo de ejecuci√≥n incorrecto",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe elegir una etapa a financiar",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Valor de desembolsos incorrecto",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        return flag;





    }


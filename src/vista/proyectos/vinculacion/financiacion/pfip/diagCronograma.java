    private void cargarCostoEjecucion() {
        if (cboetapa.getSelectedItem() != null) {
            Etapa e = (Etapa) cboetapa.getSelectedItem();
            calcularTotalEtapa(e);
            jtfCosto.setText(calcularTotalEtapa(e).toString());
        }
    }

    private Float calcularTotalEtapa(Etapa e) {


        Float total = e.getBienCapitalFC() + e.getBienCapitalFM() + e.getConsultoriasFC()
                + e.getConsultoriasFM() + e.getMaterialesFC() + e.getMaterialesFM()
                + e.getOtrosRecursosFC() + e.getOtrosRecursosFM() + e.getRecHumFC()
                + e.getRecHumFM();
        return total;
    }

    private void cambiarSaldo() {
        jtfSaldo.setText(calcularSaldo().toString());
    }

    private Float calcularSaldo() {
        String s = jtfDesembolsos.getText();
        //String d = s.replace(".",",");
        
         Float total = null ;
        try{
       
            total = Float.valueOf(s) - Float.parseFloat(jtfCosto.getText());
        }catch(NumberFormatException e){
            System.out.println(e.getMessage());
        }
        return total;
    }


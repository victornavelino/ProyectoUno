    private void aceptar() {
        if (jListTipos.getSelectedIndex() != -1) {
            String tipo = jListTipos.getSelectedValue().toString();
            switch (tipo) {
                case "PFIP":
                    diagPfip pfip = new diagPfip(null, true, "Alta", usuario);
                    pfip.setLocation(Comunes.centrarDialog(pfip));
                    pfip.setVisible(true);
                    financiacion = pfip.getPfipCreado();
                    this.dispose();
                    break;
                case "DETEM":
                    diagDetem detem = new diagDetem(null, true, "Alta", usuario);
                    detem.setLocation(Comunes.centrarDialog(detem));
                    detem.setVisible(true);
                    financiacion = detem.getDetemCreado();
                    this.dispose();
                    break;
                case "PICTO":
                    diagPicto picto = new diagPicto(null, true, "Alta", usuario);
                    picto.setLocationRelativeTo(this);
                    picto.setVisible(true);
                    financiacion = picto.getPicto();
                    this.dispose();
                    break;
                case "PICT":
                    diagPict pict = new diagPict(null, true, "Alta", usuario);
                    pict.setLocationRelativeTo(this);
                    pict.setVisible(true);
                    financiacion = pict.getPicto();
                    this.dispose();
                    break;
                default:
                    diagGenerica2 generica = new diagGenerica2(null, true, "Alta", usuario, tipo);
                     generica.setLocationRelativeTo(this);
                    generica.setVisible(true);
                    financiacion = generica.getGenericaCreado();
                    this.dispose();
                    break;
            }
        }
        this.dispose();
    }


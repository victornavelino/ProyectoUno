    private void inicializarComponentes() {
        Comunes.cargarJList(jListProyectosVinculacion, proyectoVinculacionFacade.getTodos());
     }



    private void filtrarProyecto() {
        if (!tfFiltroProyectos.getText().equals("")) {
            Comunes.cargarJList(jListProyectosVinculacion, proyectoVinculacionFacade.buscar(tfFiltroProyectos.getText()));
        } else {
            Comunes.cargarJList(jListProyectosVinculacion, proyectoVinculacionFacade.getTodos());
        }
    }



    private void agregarNuevoProyecto() {
        diagProyectoVinculacion proyectoAlta = new diagProyectoVinculacion(null, true, "Alta", usuario);
        proyectoAlta.setLocation(Comunes.centrarDialog(proyectoAlta));
        proyectoAlta.setVisible(true);
        filtrarProyecto();
    }

    private void modificarProyecto() {
        if (jListProyectosVinculacion.getSelectedIndex() != -1) {
            modificarProyectoSeleccionado();
        } else {
            JOptionPane.showMessageDialog(null, "Debe escoger un proyecto", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


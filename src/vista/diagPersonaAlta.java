    private void guardar() {
        if (validar()) {
            Persona persona = new Persona();
            persona.setApellido(tfApellido.getText());
            persona.setNombre(tfNombre.getText());
            // TODO: implementar Documento y Numero
            DocumentoIdentidad documento = new DocumentoIdentidad();
            documento.setNumero(new Long(tfNroDocumento.getText()));
            documento.setTipoDocumento((TipoDocumento) cboTipoDocumento.getSelectedItem());
            persona.setDocumentoIdentidad(documento);
//            persona.setDni(Long.parseLong(tfDni.getText()));
            persona.setFechaNacimiento(jXDateFechaNacimiento.getDate());
            persona.setSexo((Sexo) cboSexo.getSelectedItem());
            Domicilio domicilio = new Domicilio();
            domicilio.setCalle(tfCalle.getText());
            domicilio.setDpto(tfDpto.getText());
            domicilio.setEntreCalles(tfEntreCalles.getText());
            domicilio.setBarrio(tfBarrio.getText());
            // TODO: validar numero entero
            domicilio.setNumero((tfNumero.getText()));
            domicilio.setPiso(tfPiso.getText());
            domicilio.setReferencia(tfReferencia.getText());
            // TODO: falta localidad, departamento, provincia
            persona.setDomicilio(domicilio);

            new PersonaFacade().alta(persona);
            JOptionPane.showMessageDialog(null, "Se ha creado exitosamente la "
                    + "persona con el id " + persona.getId());
            this.dispose();
        }
    }

    private boolean validar() {
        boolean flag = false;
        if (LongValidator.getInstance().isValid(tfNroDocumento.getText())) {
            if (!tfApellido.getText().equals("")) {
                if (jXDateFechaNacimiento.getDate() != null) {
                    flag = true;
                } else {
                    JOptionPane.showMessageDialog(null, "Fecha de nacimiento incorrecta",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Apellido y Nombre incorrectos",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "El D.N.I. ingresado es incorrecto",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        return flag;
    }

    private void inicializarComponentes() {
        cargarComboSexo();
        cargarComboTipoDocumento();
    }

    private void cargarComboSexo() {
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        List<Sexo> sexos =  SexoFacade.getInstance().listarTodosSexo();
        for (Sexo sexo : sexos) {
            modeloCombo.addElement(sexo);
        }
        cboSexo.setModel(modeloCombo);
    }

    private void cargarComboTipoDocumento() {
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("ProyectoUnoPU",ConexionFacade.PROPIEDADES);
        List<TipoDocumento> tipoDocumentos = new TipoDocumentoJpaController(emf).findTipoDocumentoEntities();
        for (TipoDocumento tipoDocumento : tipoDocumentos) {
            modeloCombo.addElement(tipoDocumento);
        }
        cboTipoDocumento.setModel(modeloCombo);
    }
